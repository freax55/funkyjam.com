5月28日(水) 快晴/スコール<br />
<br />
ホント〜に早くもって感じなんだけど、明日の昼にはキリバスを離れ、フィジーへ戻る。<br />
暑くて暑くて、ミックスベジタブル災にも遭い、和デブに大切な氷もないし、ウィリアムは親切にしてくれたけれど、キリバス婦人の5人に3人はブラザー・トムさんと区別がつかず、過去や未来に思いを馳せたり、まーとにかくイロイロあったけれど、終わってみればアッとゆー間の1週間だった。<br />
とは言っても、巡るコトが出来たのは首都=西タラワだけだ。<br />
無人島も含めたキリバスの陸地面積は約800平方kmだが、排他的経済水域は約350万平方kmもある。ちなみに日本の国土面積は約40万平方km、経済水域は約400万平方km。<br />
アバウト比較だが、日本の500分の1にも満たない小国キリバスが、日本に匹敵するほど広大な経済水域を持っているのだ。<br />
皆さんも名前を聞いたコトがあると思うクリスマス島は、キリバス共和国の最東端にあり、国の全域が赤道直下の太平洋に点…点…点々と、かろーじて浮かんでいる孤島なのだ。<br />
だから西タラワは、キリバスの中の「・」でしかないが、そこで、よくバスにも乗ったが、よく食べ、よく歩いた。<br />
お陰で左足の子指が靴ヅレになり、持参のバンドエイドを貼り、さらに紙テープでグルグル巻きにした。<br />
今回、お茶や粉末ドリンクなんかを持って来たコトは書いたけれど、キリバスのために用意したスペシャルが2つある。<br />
1つは、テーブルタップだ。1口の電源コンセントを3口とか4口とかに増やせる、延長コードみたいなアレ。<br />
iPadやデジカメなどの充電に、複数のコンセントが絶対必要だと予測して持参した。<br />
で、大正解だった。<br />
ロッジの部屋で自由に使えるコンセントは1口だけで、バッテリー消費の激しい機器の充電に、4口のテーブルタップは大活躍した。<br />
キリバスは240ボルトだけど、テーブルタップに電圧は関係ないから心配無用。製品ごとに電流(アンペア)の許容範囲は決まってるけど、スマホやデジカメの充電くらいなら全然ダイジョブ。皆さんにもオススメです!<br />
<br />
しか〜し!<br />
コンセントの形状が日本と違う外国へ行く時は、コンセントの形状変換器を忘れないでちょーだい。その変換器を付けてからテーブルタップを差し込んでね。<br />
それからもー1つは、オナカのゆるいワタルならではのモノなんだけど、ペットボトルを利用した手動シャワートイレ（≧∇≦）<br />
空のペットボトルに水を入れ、専用のプラスチック製シャワーノズルをねじ込んで、「用」が済んだら、ペットボトルをギュッと握る。<br />
それだけで、見事に完璧に快適に、電池無用の手動シャワートイレになるのだ。<br />
ウソじゃないって。試行錯誤したと思わせるノズルの形状に、日本の職人技を感じさせる逸品で、お値段のほーが、えー、ノズル3本セットで980円…m(_ _)m<br />
それにしてもダントツに便利なのが、やはりiPadだった。<br />
Wi-Fiさえつながれば、どんな情報も日本と変わらず手に入るし、ワタルPadはminiなんだけど、こーして原稿を書くのも全然苦じゃない。<br />
インターネットは間違いなく、これからの地球人を支え、そして変化を余儀なくさせる、大革命だ。<br />
昭和なワタル自身にすれば、映画「ターミネーター」の世界が、加速度接近している感覚なのだ。<br />
ワタル電話の最初は、壁に取り付けられた木箱=電話器本体から「受器」をはずして、本体に固定されてるラッパみたいな「話器」に話すヤツ。<br />
ダイヤル直通なんて未来の話で、交換手に相手の電話番号を言って、交換手がその番号に「手動」でコードをつなぐの(^_^;)<br />
大磯から横浜になると「長距離電話」になって、番号を伝えてから受器を置いて、しばらくすると交換手から電話がかかってきて「横浜とつながりました」。<br />
横浜とダイヤル直通出来るよーになった時は、赤飯炊いてお祝いしたからね!←ウソよぉ←オギママ風にね。<br />
なのに、今は世界をつなげるインターネットの時代だから、そりゃぁ便利だし、昭和ピーポーは驚きもするっつーの。<br />
そんなワタル自身が、自ら望んでキリバスを訪れ、多くを初体験し、多くを考えさせられた。<br />
何度も書いたけれど、キリバスは温暖化の影響による海水面上昇で、このまま事態が進行すれば、今後数十年で、国土の大半が水没すると予測されている。<br />
今回出逢った子供達は、人生の後半をキリバスで過ごせないかもしれないのだ。<br />
実際、オレが泊まったロッジ周辺は、満潮になると1本しかない国道の、すぐそばまで海が迫る。潮位があと10cmも上昇したら、国道は海水に洗われる運命だ。<br />
ウィリアムが教えてくれた今年2月の異常潮位が、日常潮位になる日も遠くはなさそーな気配がする。<br />
<br />
しか〜し!<br />
キリバスが抱えている国難は、キリバスだけの国力ではどーにもならないスケールなのだ。<br />
キリバスのアノテ・トン大統領は、今こそ世界の人々がライフスタイルを変え、温暖化ガス排出を削減し、人類全体の危機に立ち向かう時だとメッセージしている。<br />
しか〜し!<br />
トン大統領には本当に失礼だが、そのメッセージを「他山の石」と見る国が、どれだけあるのだろーか?<br />
地球を守ろーなどと真剣に考えているのは、地球人の、ほんのひと握りの存在だけだ。<br />
さらに残念なコトに、そーしたSave The Earthピーポーはストイックな人が多く、地球人のライフスタイルを変えるよーな、絶大な影響力を持っていない。<br />
たとえば昭和の時代には、ヒッピーやフラワーチルドレンなんて呼ばれた人達が、反物質文化的な活動をしていたが、結局のところ、話題にはなっても本流にはなれず、時代の残滓として記憶されているだけだ。<br />
水は低きに、煙は高きに流れるよーに、人間もまた、居心地が良い方向に流れ落ち着くのだ。<br />
その証拠に、大きな影響力を持っている国のリーダー達は、自国の利益でアタマが満杯で、地球全体の将来をリアルに考えてる余裕なんてありゃしない。<br />
ロシアを、中国を、アメリカを、日本を見れば、一目瞭然だ。<br />
プーチンが、習が、オバマが、安部が、キリバスのためにナニをした?<br />
世界のリーダーが、本気で地球を救おーなんて行動したコトがあったか?<br />
アメリカと中国がサインしない「京都議定書」(1997)なんて、有名無実でしかない。<br />
今回はじめてキリバスに来て、地球の美しさに圧倒され、そこに暮らす人々にも、わずかだけれど、触れられた気がする。<br />
実は、想像していたのだ。<br />
自然保護団体の「地球を守れ!」とか「キリバスを救え!」みたいなサインが、そこらじゅーに立てられたキリバスを。<br />
<br />
しか〜し!<br />
見かけたサインのほとんどは、「ヤシの実落下に注意」や「水を大切に」や「ゴミのポイ捨てはやめましょう」など、日常生活に密接なモノだった。<br />
ワタル自身の目には、キリバスの一般ピーポーは自然の摂理に抗わず、人災をも泰然自若と受け止め、与えられた日々を精一杯エンジョイしているよーに映った。<br />
もちろん、実情は深刻に違いないが、この旅の「結び」はシンプルだ。<br />
<br />
ワタル自身は、キリバス共和国の空と海と緑と人が、赤道直下の太平洋にずっと存在するコトを願っています。<br />
<br />
世界の歴史には、気づいた時には手遅れとゆーアヤマチが、いくつもある。<br />
ワタル自身が、ベシオの壊れた砲台を見るまで、この島で命を落とした日本兵の無念が他人事だったよーに、過去は、振り返る者だけに、大切な話をしてくれるのだ……ひゃぁぁ〜、ノーパンのくせに、我ながらカッコ良すぎる終わり方だな(^_^;)<br />
