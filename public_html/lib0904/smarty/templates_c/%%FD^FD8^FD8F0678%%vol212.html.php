長かった「キリバス紀行」が、よーやく終わったと思ってホッとしてたんじゃないの?<br />
いやいやいやいや、これから「フィジー紀行」のはじまりだからね^ ^<br />
<br />
まずは時間を巻き戻して…キリバス最後の朝、ロッジの庭に出て朝焼けを撮り、部屋に戻ったらドアが開かない。<br />
この日に限って、無意識に内側のロックボタンを押しちゃったのだ。<br />
で、初代花柄ワンピにスペアキーを頼んだら、なななんと、テーブルナイフを持って来て、ドロボーみたいにカギ穴をナイフでグリグリガシガシやりはじめた。<br />
これじゃトーブン開きそーもないので、英語でハシゴが分からなかったから、全身ジェスチャーで「ハシゴがあれば」で両手を縦横に動かし、「ベランダに登って」で両足を上下に動かし、「部屋に入る」で右手でドアを開ける仕草をしてダラ汗になると、この1週間のフレアイ成果で正しく理解してくれ、初代がどこからか大きなハシゴを持って来てくれた。<br />
それで無事に部屋へ戻って帰りの荷物をまとめ、結局1度もパスポートを見せるコトなく会計を済ませ、フィジー行きの便に間に合ったのである。<br />
その機内食は、往路と同じ「ホットサンド+甘ったるいフルーツジュース+サラダ」だったんだけど、サラダがコールスローに少&#12316;しだけ「キムチの素」を加えた風味で不思議にガチウマ。機内食とはいえ、1週間振りにスーッと胃袋に落ちる食事に満足し、着陸直前まで熟睡。<br />
ナンディ空港にはトランジットでも来ているのでスムースに入国し、ネットで予約した浜辺のバンガローへ。<br />
<br />
しか&#12316;し!<br />
ここも予約が出来てなくて、代理店に確認したり、吸った揉んだ、いや、擦った揉んだで1時間くらい待たされ、よーやくバンガローに案内された。<br />
キリバスの海を観て来たワタル自身には、はじめてのフィジーの海は失望レベルだった。<br />
<br />
しか&#12316;し!<br />
ここは国際空港近くの浜辺で、そーだなぁ、超アバウトにゆーと…千葉の木更津みたいな場所だから、キリバスのグラデーション・ブルーと比べるのが論外なのかもしれない。<br />
日本のガイドブックに載っているよーな高級リゾートホテルは、空港から離れたビーチや離島にあるので、これからフィジー観光を考えてる人はワタルの意見を参考にしないでねm(_ _)m<br />
で、ここからワタルinフィジーなんだけど、自己批判的カミングアウトも含め、とりあえず書きはじめます。<br />
あのね、キリバスの海と空と風と緑は、まさに「地上の楽園」と断言しても、少しもオーバーじゃない。<br />
超自然現象とゆーと、人智を超えた不可思議な現象のコトだけど、キリバスは、太平洋に浮かぶ、現実の超自然現象なのだ。<br />
<br />
しか&#12316;し!<br />
一般ピーポーの観光地としては、あまりに不便で、あまりにナニモNothingだ。<br />
そんなキリバスからフィジーに来ると、ナンディ空港のあるヴィチレヴ島は大陸とも思えるスケールで、全長500mほどのメインストリートを持つナンディタウンは大都会だ。<br />
今回のバンガローも全体的にC級だけど、キリバスのロッジに比べたら超高級リゾートホテル。レストランはあるし、バーはあるし、アイスクリームを売っているし、すべてチープだけれど、観光客をもてなそーとするベクトルが伝わる。<br />
そして、部屋に荷物を置いたワタル自身の第一声が、「いやぁ&#12316;極楽だなぁ&#12316;!」で、自分でもハッとした(ｰｰ;)<br />
氷もある、酒もある、街へのシャトルバスもある、タクシーもある…飛行機でたった3時間のキリバスにはなかったすべてが、ここにはすべてあるのだ。<br />
ナンだ、この現実は&#8265;<br />
ナンなんだ、この格差は&#8265;<br />
そしてそれを極楽と言っちゃうワタル自身は、都会生活にひたり切った軟弱下世話な和デブでしかない。<br />
