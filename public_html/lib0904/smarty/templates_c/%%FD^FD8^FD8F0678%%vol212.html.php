んーんー、もちろんゴーインな設定だって分かってるけど、ここからキリバスとフィジーの精神的格差がツマビラカになるんだよ。<br />
えーとねぇ、山手線の通勤満員電車で、車内広告を見ていたワタル自身が、不意に女子高生に右手をつかまれ「このオジサンがアタシのオシリをモミモミしましたっ!」って言われたワタル自身が渋谷警察署に連行1泊の翌朝、無実の罪だと分かり釈放されて、センター街のマクドナルドでモーニングコーヒーを飲んで思わず「いやぁ&#12316;極楽だなぁ&#12316;!」って背伸びしたのと、フィジーの「いやぁ&#12316;極楽だなぁ&#12316;!」はマッタクもって同一精神解放発言なのだ。<br />
だから自己批判で落ち込む必要はなく、正々堂々と、キリバスからフィジーに戻って「いやぁ&#12316;極楽だなぁ&#12316;!」とヒラキナオッタのだった。<br />
<br />
しか&#12316;し!<br />
フィジーでのプランは実にアバウトなのよぉ←忘れてないよね、オギママ風だよ^ ^<br />
どーせトランジットで降りるなら、2度と来ないかもしれないフィジーも観てみよーと思っただけなのだ。<br />
