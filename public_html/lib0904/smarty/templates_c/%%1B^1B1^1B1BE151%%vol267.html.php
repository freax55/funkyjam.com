趣味と道楽の差はナニか?<br />
広辞苑で「道楽」を引くと、こんな解説があった。<br />
<br />
【道楽】(道を解して自ら楽しむ意から)1-本職以外の趣味などにふけり楽しむこと。また、その趣味。「釣道楽」2-ものずき。好事(こうず)。3-酒色・博打(ばくち)などの遊興にふけること。放蕩。遊蕩。また、その人。浮世床。「人も三十越して浮世床になったのはむづかしいよ」。「浮世床で身を持ち崩す」。<br />
<br />
なるほどナルホド。<br />
落語に登場する若ダンナは、大抵が道楽息子で親に勘当されたりするのは、酒色・博打・放蕩・遊蕩・浮世床だからなのだ。<br />
<br />
しか〜し!<br />
それでも、とゆーより、それだからワタル自身は、道楽とゆー言葉が大好きだ。<br />
道楽には、裕福で自由で、もっと言えばファンキーな響きがある。<br />
だから、全国ツアーを何ヶ所も観に来てくれる、久保田利伸のオールドファンは、もはや完璧なる久保田道楽である。<br />
久保田道楽…長寿のオモムキすら漂っている。<br />
あら!? 村山のビリヤード世界戦atラスベガスの話をしていたのに、いつの間にか道楽者の話になっちゃった。<br />
