今回の原稿は東京からラスベガスに送られ、また東京に戻されて掲載されている。<br />
どーしてこーゆーコトになったかとゆーと、ファンキー・ジャムで週刊ワタル自身を担当している村山英世が、なななんと、ラスベガスで試合をしているからなのだ。<br />
<br />
しか〜し!<br />
ラスベガスで試合と言っても、ボクシングのタイトルマッチではない。<br />
ホワイトアスパラみたいな体躯の村山にとって、格闘技はカメの腹筋運動と同じレベルで不可能だ。<br />
村山は、アマチュアのビリヤード団体JPA(Japanese Poolplayers Association)の日本代表チームの一員として、本場アメリカの大会(National Team Championships)に挑むためラスベガスへ行っているのだ。<br />
どーだ、スゴイだろ!?<br />
オレが自慢しても仕方ないが、どんな競技でも身近な者が日本代表になるのは誇らしい。<br />
普段の村山はレコーディング・エンジニアとして久保田利伸に夜明けまでコキ使われ、ツアーがあると全会場のスミッコで目立たないけど大事な仕事をし、たまに久保田の代わりにリハーサルで歌わされたりしている。<br />
若い音楽業界ピーポーは、一般的な同世代よりも時間が不規則なので、プライベートな予定が立てにくい。<br />
<br />
しか〜し!<br />
そんなスケジュールをやりくりし、大好きな趣味=ビリヤードで日本代表に選ばれた村山は、日陰で育つホワイトアスパラらしからぬ、実に見上げた根性をしている。<br />
趣味のない人を否定しないが、没頭できる趣味は、上等なセラピー以上の価値がある。<br />
損得や勝ち負けにこだわる者もいるが、たとえば、魚が全然釣れなくても裕福なココロになれなければ、釣りが趣味などと言ってはならぬ。<br />
<br />
しか〜し!<br />
ワタル自身は、趣味よりも道楽とゆー言葉に惹かれてしまう。<br />
「実は最近…」で右手の小指を立て、「オンナ道楽にハマってまして…」なんて、オトコの夢である。<br />
ストレス発散に買い物をする女性がいるが、それなどまさに趣味を超えた見事な道楽だ。<br />
<br />
しか〜し!<br />
