森大輔の配信シリーズ第三弾でリリースした「&nbsp;<span ><a href="http://m.funkyjam.com/artist/mori/news/2015/06/10.html" style="font-size: 16.25px;">はだしのシンデレラ</a>&nbsp;</span>」がHuluオリジナルトークバラエティ「座長は語る!」（9/30より配信開始）のテーマソングに決定!!!<br>
《Huluオリジナル》ここでしか見られない、素顔のリーダー「座長」に迫るトークバラエティ「座長は語る！」9月30日より配信開始<br>
オリジナルコンテンツの最新作「座長は語る！」が今秋Huluより配信開始になります。各事務所の代表格・リーダー的存在の「座長」がナビ役となり、所属タレントと絡みながら「座長」ゆえの愛ある目線でトークを繰り広げるトークバラエティになります。従来の座って語るトーク番組と違い、普段見る事の出来ない稽古場や衣裳部屋で座員と話す様子や、メイク中のリラックスした雰囲気で質問に答える様子など貴重な映像が満載。対談やオフショットに加え、ネタ見せなど盛り沢山な内容になっています。まさに、お笑いリスペクト！<br>
第一回目はワハハ本舗、久本雅美さん。「久本雅美に100の質問」と題し、様々な場所で突然のムチャブリ質問にアンサーしたり、後輩へのメッセージ、美と健康の秘訣など素顔の久本雅美さんに迫ります。座長だからこそ感じる苦悩や座員への愛が垣間見える作品になっています。<br>
