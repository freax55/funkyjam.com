※5/宇崎竜童(うざき・りゅうどう)=1946年、京都府出身。73年にダウン・タウン・ブギウギ・バンドのヴォーカルとしてデビュー以降、夫人の阿木燿子と組んで多数のヒット曲を生み、プロデューサーとしても活躍。<br />
