1960年代後半から70年代中期は、和製フォークソングの全盛期で、ワタル自身の思春期とピッタリ重なる。<br />
<br />
しか〜し!<br />
思春期ゆえに共感出来なかった歌手や楽曲も少なくない。<br />
高校の修学旅行では退屈だった京都も、年を重ねると魅入られるよーに、思春期は視野が狭く、異質の世界が受け入れられなかったのだ。<br />
で、思春期ワタルが理解出来なかったフォークシンガーの1人に、友川カズキ(※1)がいる。<br />
友川の音楽は、とにかく暗く、激しく重く、ザラザラする言葉を放ち、ギターのストロークは弦が切れんばかりに暴力的で、歌はメロディを飛び出して叫びになる。<br />
このサイトを読んでいる方の多くは久保田利伸系の音楽が好きだと思うが、友川の歌は、久保田の対極にある。<br />
同時代に三上寛(※2)がいて、この人もソートーに暗くて暴力的なんだけど、友川カズキに比べたら極上のポップス…そのくらい友川には大衆性もスター性もなかった。どっちかとゆーとイケメン系なのに、彼の歌が社会を拒否していたのだ。<br />
そもそも、中学生の時に中原中也の「骨」(※3)を読んで触発されたとゆーから、フツーの子供じゃなかったのである。<br />
高校ではバスケットボールに情熱を注ぐも、岡林信康(※4)でフォークソングに目覚め、宇崎竜童(※5)に見出されて75年＝25歳でアルバムデビュー、当時では激しく遅咲きのシンガーだ。<br />
最近ではナインティナインの岡村隆史が友川に興味を持ってラジオ番組で紹介したりしているが、彼の精神世界は「骨」から微動だにしていない。<br />
そこでワタル自身は、思春期の難敵にピリオドを打つべく、友川カズキのソロライヴへ出かけるコトにした。<br />
この40年で、友川カズキの歌が分かるくらい、ワタル自身は成長したのだろーか?<br />
会場は小岩のライヴハウスで、場所をウェブしていたら指がすべって「大竹」とゆーワタル好みの飲み屋を発見してしまい、とりあえず焼酎とガツ刺しとジャガイモ入りの絶品煮込みでアクセルを吹かし、さらに2年連続でミシュランガイドに載ったラーメン「えどや」で満腹になり、ヨーヤク目的地のライヴハウス「ブッシュ・バッシュ」に到着する。<br />
タバコ臭い狭い地下スタジオに寄る辺ない男どもがウジャウジャかと思いきや、明るい1階フロアに白木のテーブルがまばらに並べられ、平均年齢45歳とおぼしき男女が20人ほどリラックスしていた。<br />
開演時間の20時を過ぎても、客は3割…いや、2割程度。こんな場所で、友川カズキは暗く重く叫ぶのか？<br />
<br />
しか〜し!<br />
安心してください。<br />
本当のライヴスペースは隣の部屋にあり、そこには40人ほどのダークサイド愛好家が地道に開演を待っていた。<br />
それでも久保田利伸のライヴを観慣れているワタル自身には、背筋が寒くなるよーな状況だ。<br />
<br />
しか〜し!<br />
10分遅れで開演した友川カズキのライヴは、意外にも、笑いからはじまった。<br />
競輪の予想が大当たりした友川は上機嫌で、いかにも昭和フォークシンガーらしい軽妙なトーク…それは古今亭志ん朝の枕噺と同じよーに、歌へつづく言葉のイントロになっている…久々に昭和フォーク伝統のお笑いトークに触れ、モーレツに嬉しくなる。<br />
<br />
しか〜し!<br />
歌は暗い。やっぱり激しく暗かった。<br />
40年前と全然変わっていない。<br />
20分ほどの休憩をはさみ、ワタル的にゆーと「アンプラグド・ソロパンク」な叫びとギターが2時間近く。鋭い毒もきっちり吐き、ステージの友川カズキは、思春期ワタルの友川カズキそのままだった。<br />
<br />
しか〜し!<br />
それは1975年と2016年が一直線につながっている、彼の人生そのものでもある。<br />
本人いわく「ヒット曲もない」のに、40年も歌いつづける不滅の情熱には、同世代の男として脱帽するしかない。<br />
<br />
しか〜し!<br />
「同じよーな曲を作りすぎて自分でも区別がつかなくなっている」ので、観客初心者のワタル自身が、ほとんど全部同じ旋律に聴こえたとしても、それで退屈したとしても、誰が責められるとゆーのだろーか!? かっかっかっ!?(←友川カズキの真似)<br />
さらに、秋田ナマリが現在進行形で、ボスがブスに聴こえ、キミがチミに聴こえ、暗く重い歌詞なのに意味を取り違えて笑ってしまったとしても、誰が責められるとゆーのだろーか!? かっかっかっ!?<br />
友川カズキ…いまだに彼の音楽世界には入り込めなかったけれど、メロディから飛び出す言葉の破壊力に直撃され、思春期の難敵にピリオドではなく、今のワタル自身に風穴を開けられた夜となった。<br />
