<br />
<br />
<center><font color="#FFBF17">「村上春樹/色彩を持たない多崎つくると、彼の巡礼の年」</font></center><br />
<br />
「村上春樹さんのこの作品をようやく読むことができました。この人の作品はタイトルにトリックがあることが多いので、今回の作品も“変なタイトルだなぁ〜”と思った。タイトルだけじゃ、どういう話なのか全く予測できないよね。<br />
この本は結構話題になったようなので、読んだ人はたくさんいると思うけど・・・流石に大先生だけあって、読み物としてはとても面白い！本を読んで何かを学ぼうとか、例えば人生について考えるとか、そういう堅苦しいことを目的としないで、ただただエンターテインメントとしての本という意味で最高でした。いい意味での現実逃避ができて、違う時間に連れていってくれる。<br />
そして、余計なことが書かれていなくて嬉しい。どんな作家でも、読んでいるとちょっと疲れてくるような退屈させるページがあったりするんだけど、この本はそう感じさせるページがなかった。」<br />
<br />
<br />
<font color="#FFBF17">　― 村上さんの作品はこれ以外にも読まれているようですが、どんなところに魅力があるのでしょうか？</font><br />
<br />
「何よりも人を楽しませようという気持ちがすごく強いと思う。だから、超・読書家じゃない僕でも最後まで楽しく読めるんだよね。そういった部分が世界に認められているんだと思う。難しいことを、難しい舞台で描くのではなくて割と日常に近い部分で描かれているから画が想像しやすい。それと、ときどきエロいシーンを挟んでくれるので飽きない（笑）。<br />
あとは、本当に知識が豊富だよね。歴史、音楽、政治、医学の事・・・勉強しているんだと思うけど。今回の作品もよくできた作品だと感心しました。」<br />
