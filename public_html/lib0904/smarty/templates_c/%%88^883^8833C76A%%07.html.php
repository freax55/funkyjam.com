Track.07<br /><font color="#ffcc00">「Is it over?」</font><br />
<center><img src="../image/timelessfly.jpg" /></center>
<img src="../../../../images/common/dot.gif" width="1" height="10" /><br />
別れていくことの切なさを歌う曲のイメージに「この曲にはどうしても<font color="#ffcc00"><strong>JUJU</strong></font>の声が欲しい」という久保田の希望のもと、制作がスタートした楽曲です。<br />
<br />
そんなこの曲の核となるJUJUのボーカルは、毎回のサビと、エンディングにループとして登場します。<br />
「最もJUJUらしさが発揮できるように」ということで、ボーカルを収録する前のカラオケのレコーディングでは音程を3パターン用意し、その中から一番JUJUのキーに合う音程を選ぶというほど、こだわって制作されました。<br />
<br />
その甲斐もあり、歌い始めてみるとイメージに合い過ぎというくらい合っていて、何の問題もなく順調にレコーディング作業は進みました。<br />
予定よりも早い進行だったので、サビ以外にも何か足してみようかというスタッフからの提案もありましたが、「サビだけに出てくるほうがよりインパクトがあるし、より贅沢」という久保田の思い入れが強く、予定通りサビだけでのパフォーマンスとなりました。<br />
<br />
お互いニューヨークでの共通の知人や話題などもあり、和気あいあいな雰囲気で終始レコーディング作業は進められました。最終段階ではJUJUの歌声を聴きあらたなインスピレーションが生まれた久保田が、さらにかけ合いのアドリブを録り直し、両者の対比がとても印象的なサビができあがりました。<br />
