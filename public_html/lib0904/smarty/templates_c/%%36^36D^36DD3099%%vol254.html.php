「私は確信したい。人間は恋と革命の為に生まれてきたのだ」と名作「斜陽」で書いたのは太宰治だが、ワタル自身もこんな鋭利な名言をモノにしたいと思いつづけている。<br />
今日までのワタル自身名言最高傑作は、「味の素は隙間を埋める」だ。<br />
味の素の存在価値については、以前きっちり書いたので今回は詳しく触れないが、今ひとつ味が物足りない時に、味の素をパラリと入れるだけで、あ〜ら不思議、味の隙間がピタリと埋まるのだ。<br />
「味の素は隙間を埋める」…シロートにはなかなか言えない、元ラーメン職人ならではのウンチク深い名言である。<br />
<br />
しか〜し!<br />
いかに真理とは言え、太宰とは世界観が違い過ぎている。<br />
<br />
しか〜し!<br />
最近知ったんだけど、なななんと、太宰治は味の素の大ファンだったのだ。<br />
大ファン…とゆーより、むしろ中毒的で、ヨーカンやマンジューにまで味の素を多量に振りかけていたのだ。<br />
檀一雄の「小説　太宰治」には、太宰が「僕がね、絶対、確信を持っているのは味の素だけなんだ」と語った事実が書かれている。<br />
そーだ、そーなのだ。太宰にとって恋と革命は「確信したい」レベルだったのに、味の素だけは絶対に「確信」していたのだ!　だっだっだっ!<br />
さらに調べてみると、自著「HUMAN LOST」の中で「私は、筋子に味の素の雪きらきら降らせ、納豆、青のり、と、からし、添えて在れば、他には何も不足はなかった。」とも書いている。<br />
さすが大文豪!　「味の素の雪きらきら降らせ」なんて、一般ピーポーには絶対書けない。<br />
<br />
しか〜し!<br />
筋子＋納豆＋青のり＋からし＝他には何も不足はなかった…って、こんだけありゃぁ不足なんてあるワケないっつーの。<br />
このよーに、偉人は時として偏人となるのだ。<br />
スウェーデンのカール・リンネは、18世紀中期に名を成した分類学者で、現在も「分類学の父」と敬称されている。<br />
カール先生はクジラが魚よりも牛に近い哺乳類だと最初に気づいた人物でもあるが、特に植物の分類に絶対の知識と自信と実績を誇っていた。<br />
<br />
しか〜し!<br />
先生は、超ド級にエッチが大好きな人だった。<br />
仕事の最中もエッチなコトばかり考えていて、植物の雄シベや雌シベに男女のイトナミ的な名前をつけたり、貝類なんかにはヤバくて書けないよーな女性の淫部名をつけたりした。<br />
まーとにかく、下品きわまりないネーミングだらけなのだが、カール先生の分類学により、それまでアイマイだった系統がきっちり整理されたので、みんな文句を言えなかった。<br />
<br />
しか〜し!<br />
一般ピーポーも生物学を学ぶよーになり、学校の教科書にR20言葉が満載はイカガナモノカとなり、次第にカール先生のネーミングは使われなくなったのである。<br />
やはり、偉人は時として偏人となる。<br />
かつて、渋谷に「三漁洞」(※1)とゆー酒場があった。<br />
オーナーは福田蘭童…と言っても知らない方が多いと思うが、戦前に活躍したミュージシャンだ。<br />
ピアノやバイオリンも弾いたが、尺八に天才があり、作曲家としてもラジオ番組や映画音楽などを手がけた。<br />
<br />
しか〜し!<br />
ワタル自身が蘭童の名前を知ったキッカケは、音楽家としてではなく、釣り名人としてだった。<br />
開高健の著書に釣りの天才=福田蘭童の名前を見つけ、彼の足跡をたどり、渋谷の「三漁洞」も知ったのである。<br />
釣り師としての蘭童は、圧倒的なレジェンドで、魚が釣り針に近づく水流の微妙を竿の手元で感じ取り、魚種や大きさまでも分かったと言われている。<br />
本命以外の魚が近づくと、わざと竿を上げてよけたとも言われている。<br />
また、釣った魚を料理する腕も一流で、「三漁洞」は彼の終の住処となった。<br />
<br />
しか〜し!<br />
蘭童もまた、とてもとてもエッチが好きだった。<br />
いつイザとゆー時が訪れてもOKなよーに、常にコンドームを持ち歩き、釣りをしながらもエッチなコトばかり考えていて、釣り針にコンドームを付けると魚がエサと間違えて食いつくのを発見し、コンドーム疑似餌(※2)を開発した。<br />
<br />
しか〜し!<br />
釣り専用のコンドーム疑似餌までなら良かったが、「陸釣り」も得意な蘭童は、結婚詐欺や違法賭博なんかで捕まったりもした。<br />
<br />
しか〜し!<br />
これで反省しないのが偉人偏人で、蘭童は映画撮影で伊豆大島へ向かう船中で、コトもあろーに、主演女優をレイプしちゃうのである。<br />
これで蘭童は「色魔蘭童」と呼ばれるが、きっちり責任をとって(?)本妻と離婚し、レイプした女優と再婚する。<br />
<br />
しか〜し!<br />
蘭童の才能はその後も衰えるコトなく、大ヒットしたNHKラジオ番組「笛吹童子」の音楽ディレクターなどを務め、その後も釣り師&料理人としての名声も高めたのである。<br />
ちなみに、YouTubeで「笛吹童子」(作曲:福田蘭童)を検索すると、イントロで彼自身の尺八が今も聴ける。<br />
ちなみにONE MORE、蘭童と前妻との間に生まれた息子は、「ハナ肇とクレージーキャッツ」のキーボード担当から料理家に転身した石橋エータローで、蘭童の父親は画家の青木繁(※3)である。<br />
かくして、偉人は時として偉人を遺し、偏人を凌駕する。<br />
