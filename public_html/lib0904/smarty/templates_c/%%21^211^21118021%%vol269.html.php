実写版の映画「進撃の巨人 ATTACK ON TITAN」を観た。<br />
ネットではボロクソに言われていたけど、ラーメンと映画の評価はマッタク信じないワタル自身なのである。<br />
で、案の定…面白かった*^^*<br />
原作とは違う設定が気に入らないとか、若い役者の演技が納得いかないとか、批判的に観れば不満があるかもしれないが、ワタル自身の大好きな石原さとみちゃんが頑張っているし、久々に日本の伝統芸「特撮」が堪能出来て楽しかった。<br />
<br />
しか〜し!<br />
エンディングロールを観ていて「あれ!?」となった。<br />
エンディングロールに「KREVA」の名前があったからだ。<br />
KREVAが出ていたなんて、ゼーンゼン気がつかなかった!<br />
もしかしたら、あの役がKREVAかな…と思ったりもするが、確証がない。<br />
KREVAは9月19日に公開される後編「進撃の巨人 END OF THE WORLD」にも出演するとゆーから、その時は最初からKREVA探しだ。<br />
映画の好みは人それぞれだが、ワタル自身は迫力ある映像を大スクリーンで楽しめる作品が好きだ。<br />
なので、今年になって劇場で観たのは、以前紹介した「バードマン あるいは(無知がもたらす予期せぬ奇跡)」の他には、「アベンジャーズ/エイジ・オブ・ウルトロン」「メイズ・ランナー」「ターミネーター：新起動/ジェニシス」「マッドマックス/怒りのデス・ロード」「ジュラシック・ワールド」etc…まるで子供レベルである。<br />
大スクリーンでなくても楽しめる作品、たとえば、ラストで号泣しちゃう恋愛映画や、緻密な謎解きミステリーなんかは、ワタル部屋で酔っぱらったり泣いたりしながらオンデマンドやレンタルDVDで楽しむコトにしている。<br />
とにかく映画は、以前にも書いたと思うけど、ワタル自身にとって一番手軽に楽しめるエンターテインメントなのだ。<br />
しかも今年からシニア料金で観られるよーになり、今までよりも更に身近&気軽になった。1800円が一気に1000円なんだから、そりゃ嬉しい嬉しい^o^<br />
で、シニア料金をエンジョイする映画ピーポーは多く、平日初回の上映は早起き老人の集会場と化している。<br />
映画関連団体の努力によるシニア料金実施は、映画館にとっても高齢者にとっても大いに有意義なプランで、政府の指導で行われている全国各地のプレミアム商品券なんかより、映画ファンにはよっぽどアリガタイ^o^<br />
<br />
しか〜し!<br />
先日、ちょっとしたトラブルがあった。<br />
映画が終わったので、電源をOFFにしていたスマホをONしよーとしたら、ウンともスンとも反応しないのだ。<br />
強制起動を試してもピクリともしない。<br />
振っても叩いても揉んでも拝んでもマッタク目覚める気配がない。<br />
仕方ないので犬のお父さんショップに行くと、本体のトラブルはアップルストアへ行ってちょーだいと言われた。本体修理や交換には3万円くらいかかるとも言われた。<br />
なので、かなりビビリながらアップルストアへ行くと、10分ほどして担当者が登場してトラブルを確認すると、13時にまた来てちょーだいと言われた。13時まで1時間45分もある。<br />
カンレキ未満ツーフー以前のワタル自身なら、ここで文句のひとつも発したが、今や達観の域に達し、0.5ミリも動じるコトなく〈I will be back at 13〉となる。<br />
<br />
しか〜し!<br />
13時に再訪してから、さらに壁際で10分待たされ、テーブルに案内されて10分待たされ、よーやく担当者が出現。ちょっとイラっとなった。<br />
スマホをチェックしながらナンだカンだと早口で説明され、とにかく電源が入らない事実が認められた。<br />
すると担当者から、早口で「無償」とゆー単語が出た。<br />
『もしや!?』と思い耳を傾けると、今度はハッキリと「本体の無償交換になります」と確認出来た。<br />
そのトタン、それまでのイライラが霧散した。<br />
トーゼンである。3万円だと思っていたのがタダになったのだ。懐中不如意のワタル自身が嬉しくないワケがない(o^^o)<br />
iCloudのオカゲで保存していたデータもそのまま、新品のスマホを受け取り笑みがこぼれ、『ナンならまた1時間待ってもイイからね』の聖人になる。<br />
映画のシニア料金もスマホの無償交換も、やっぱり国より民間のほーが、庶民の生活に直接届くアリガタイ努力をしている。<br />
この国は、カネを詐欺してトンズラする国会議員がいたりするけど、庶民は庶民による庶民のための日本を忘れてはいないのだからね!<br />


