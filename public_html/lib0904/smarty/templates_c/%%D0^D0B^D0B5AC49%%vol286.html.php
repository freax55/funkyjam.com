ベトナムとゆーと、ワタル世代はベトナム戦争(1960~75)を思い出さずにはいられない。<br />
東南アジア大好きのワタル自身がベトナムに二の足を踏んでいたのは、実はワタル人生の中でベトナム戦争が、一番身近に感じた戦争だからでもある。<br />
戦争以前の背景に触れると大日本帝国なんかも登場して大変マルなので、激しく簡単にゆーけど、ベトナム戦争は北ベトナムVS南ベトナムとの覇権戦争だ。<br />
当時のベトナムは、ダナンの北隣にある古都フエを流れているベンハイ川=17度線で南北に分断され、北軍を支援していたのがソビエト・中国・北朝鮮などのアンチ民主主義国で、南軍支援がアメリカ・韓国・中華民国(台湾)などの民主主義国だった。<br />
ヨースルにベトナム戦争は、ベトナム全土を戦場にした民主主義VS共産主義…結果的にはアメリカVS共産圏の代理戦争だったとも言えるのだ。<br />
平成ピーポーも「ベトコン」とゆー言葉を聞いたコトがあると思うが、ベトコンは正式名「南ベトナム解放民族戦線」の差別用語で、太平洋戦争時にアメリカ人が日本人を「ジャップ」などと蔑称していたのと同じなのだが、米軍経由で日本のマスコミにもベトコン=北軍ベトナム兵の呼称みたいに定着した。<br />
「ベトナム戦記」を著した開高健氏も、北軍ベトナム兵士=南ベトナム解放民族戦線=National Liberation Frontと理解した上で「ベトコン」と記しているので、ここでもベトコンの呼称をお許し願いたいm(_ _)m<br />
<br />
しか〜し!<br />
上から目線のアメリカだったが、ベトコンの奇襲攻撃でコテンパンにされてしまい、アタマに来て1965年から北側に爆撃機を飛ばしまくり、悪名高き「北爆」を開始する。<br />
この時に落とされた爆弾は、太平洋戦争でジャップ本土の爆撃に使われた爆弾の、なななんと20倍もの量で、いかにアメリカがベトコンに激怒していたかが分かる。<br />
<br />
しか〜し!<br />
それでもベトコンは古タイヤで作ったホーチミンサンダルでジャングルを駆けまわり、地下に網の目のトンネルを掘り、最新兵器で武装した米兵を映画「ランボー」さながらのゲリラ戦術で恐怖に陥し入れて徹底抗戦した。<br />
そして1975年3月=ワタル自身が20歳になったばかり、ついに南の首都サイゴン(現ホーチミン)を陥落し、大国アメリカが支援した南軍チームに勝利したのである…って、たったコレだけの書き飛ばしでも、ベトコンの凄みが伝わったんじゃないかな？<br />
戦争当時のダナンは北爆の最前線基地で、沖縄・岩国・グアムの米軍基地からも爆撃機が北ベトナムに飛び立っていた。<br />
平成ピーポーにはショックかもしれないけれど、ベトナム北爆には日本の基地が直接的に関与していて、嘉手納を飛び立つB52爆撃機や、岩国に帰還した負傷兵の姿など、TVニュースの記憶は今もワタル自身に鮮明なのだよ。<br />
それから40年、ダナンの歴史博物館でベトナム戦争の残滓を目の前にした。<br />
展示ケースの中のボロボロのクタクタの、粗末な装備には今さらながら「ベトコンの不屈」を感じ、米軍が投下したナパーム弾の大きさには、身震いせずにはいられなかった。<br />
ナパーム弾は1000度にもなるドロドロの火焔ゼリーを四方八方に撒き散らし、あたり一面テッテーテキに焼きつくす焼夷爆弾だ。そればかりか、爆発時に多量の酸素を瞬時に消費しするので、ドロドロ火焔ゼリーを逃れた人も動物も、ことごとく窒息死する。<br />
キリバスの海岸に遺された迎撃砲にも落涙したが、実際に使用された兵器には、恐怖と死と絶望と怨念が封印されているばかりで、平和や正義や良心など微塵も感じられない、<br />
<br />
しか〜し!<br />
さらに米軍は、ナパーム弾より悪魔的な「枯葉剤」をベトコンが潜むジャングルに撒き散らし、土も水も空気も人もすべてを汚染し、罪のない一般ピーポーの奇形児出産など大問題を残した。<br />
街並から戦争の陰は消えた今も、ベトナム戦争の悲劇は長い長い尾を引いているのだ。<br />
<br />
しか〜し!<br />
ここからワタル余談を少々(._.)<br />
米軍の非道が伝わるにつれてアメリカでは反戦運動が高まり、ボブ・ディランやジョーン・バエズ、ピーター・ポール&マリー等が歌う反戦歌に触発され、日本でもオリジナルの反戦歌が生まれてフォークソングが一気にブームとなった。<br />
ジョン・レノンやマーヴィン・ゲイ、エルトン・ジョンetc.…など多くのアーティストも反戦歌を歌っていたが、当時の日本は音楽的に単純な4コードのフォークソングが受け入れられ、湘南のワタル小僧もベトナム戦争の実態も知らないくせに、高校2年で初めて作ったオリジナル曲が「戦場に咲く花」…とにかく思春期ド真ん中だったのである。<br />
ちなみに久保田利伸も最初のオリジナル曲は高校生の時に作った「ジャマイカに帰ろう」で、行ったコトもないジャマイカに帰ろーとしていた…男の思春期とゆーのは、トカクそーゆーモノなのである(^_^;)<br />
えー、話をベトナムに戻します。<br />
そして戦後40年=2015年のベトナムは、ベトナム共産党が一党支配する社会主義共和国になっているが、世界中の観光客をWelcomeしてくれる国になった…よーに見える。<br />
南沙諸島に基地を築いている中国にはアメリカと協力して対抗しているし、日本からも多数の企業が進出しているから、日本人観光客にすれば「イイ国じゃん」である。<br />
前出「ベトナム戦記」で、ベトナム仏教の僧侶が「はじめのうちは共産主義者は宗教に対してひどくやわらかで妥協的ですが、やがて宗教を否定し、弾圧します。かならずそうです」と語っているが、その仏僧の予言は今はまだ当たっていない…よーにも見える。<br />
これも「ベトナム戦記」の仏僧発言の要約だが、「ベトコンのほぼ100パーセントが農民仏教徒で、本物の共産主義者は1パーセントくらい」…が真実なら、これからもベトナム共産党は仏教徒を弾圧しないよーな気がするし、是非そーあり続けて欲しいと切に願うm(_ _)m<br />
<br />
ホイアン市場近くのカフェの壁に、古式ゆかしい結婚式と思われるモノクロ写真が飾ってあったので、「これはベトナム戦争以前の写真ですか?」と訊くと、店主が笑顔でこー言った。<br />
「これは7年前のオレの結婚式の写真だよ。ほら、ここにオレが写ってるだろ」<br />
こんな会話にカンレキ独り旅のワタル自身は、平穏な日々とゆーのはナニモノにも変えがたい…そうしみじみと実感する。<br />
<br />
しか〜し!<br />
世界は2015年も平穏に遠く、無力なワタル自身は大晦日に酔って候m(_ _)m<br />
