浦嶋りんこ（うらしまりんこ）<br />
</center>
<br />
1992年、久保田利伸のバックグラウンドヴォーカリストとしてプロデビュー。<br>
その後、DREAMS COME TRUEのバックヴォーカリストを務め、別ユニット「FUNK THE PEANUTS」で人気を博す。様々なアーティストに関わるも、1999年、ミュージカル「RENT」に起用され、音楽のみならず芝居における新境地を拓いてゆく。2005年、鈴木清順監督作品「オペレッタ狸御殿」で映画デビューも果たす。<br>
また、2011年から始まったNHK教育の子供向けパペットバラエティー第三弾「フック ブック ロー」では、レギュラーで声優として出演している。<br>
2013年には、ミュージカル「レ・ミゼラブル」新演出ver.にてマダム・テナルディエ役に大抜擢され、多方面から絶賛される。2015年にも再演が決定し、今回も同役で出演予定。<br>
2014年、出演した「フル・モンティ」「シスター・アクト〜天使にラブソングを〜」は、大盛況で幕を閉じ、現在、DREAMS COME TRUE TOURに出演中。 パワフルな歌声と演技力で、あらゆるジャンルに対応可能なマルチアーティストとして、益々活躍の場を広げている。
