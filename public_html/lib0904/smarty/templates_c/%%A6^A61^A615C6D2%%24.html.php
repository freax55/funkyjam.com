<font color="#FFCC00">森大輔　オフィシャルファンクラブ「モリノナカマ」新規会員募集中！</font><br />
<img src="/images/common/dot.gif" width="1" height="8" /><br />
いに、オフィシャルファンクラブ「モリノナカマ」が遂にグランドオープン致しました！どこよりもいち早く森大輔のニュースをお知らせする「モリにゅー。」、これまではクッキングがメインでしたが、料理に限らず森のオフムービーを大放出する予定の「モリチャンネル」、他にも、大好評の４コマ漫画「モリコミ」あらゆる角度から森を捉えたフォトを放出する「モリスタグラム」、スタッフが素の森をレポートする「モリ裏」、そしてそして今回から新たに追加となった「モリ推し」こちらはタイトル通り森大輔が自信を持ってオススメするモノ・コトを本人の言葉でご紹介致します！もう一つのニュー・メニューは「モリ部屋」。ここではファンの皆さんと森大輔が交流できる場所！というコンセプトのもと皆様から頂いたご意見、ご感想等に、森がお返事したりしなかったり(!?)というコーナーになります。他にも様々な特典もご用意しています！<br>
この機会に是非「モリノナカマ」の一員になって、森大輔を応援しませんか！？皆様のご入会お待ちしております！<br>
