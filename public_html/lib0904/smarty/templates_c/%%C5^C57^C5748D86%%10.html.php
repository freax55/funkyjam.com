絶大な人気と圧倒的実力を兼ね備えたアジアR&amp;B 界の至宝BROWN EYED SOULのメンバーが、これまでにリリースしてきたソロ作品のなかから選りすぐりの曲を1枚にコンパイルした日本オリジナル企画アルバム！メンバー4人それぞれが作詞・作曲家で、ソロ活動も精力的に行っている彼らだからこそ創ることができる作品であり、ひとり4曲ずつ計16曲のソロ曲を収録。この企画アルバムのために新たに1人1曲ずつ日本語歌唱で再レコーディングしており、韓国語歌唱曲12曲＋日本語歌唱曲4曲の計16曲となっている。気になる16曲は、ジョンヨプは数々のアーティストにカバーされ続けている名曲「Nothing Better」の日本語バージョンを、ナオルは韓国にて2015年上半期ダウンロード数1位を獲得した「同じ時の中の君」を、ヨンジュンは人気ラッパーSimon Dとコラボした「花よりも君が」を、ソンフンは根強い人気を誇る「With Chocolate」の日本語バージョンなど珠玉の16曲が散りばめられたスペシャルな内容となっている。さらに、特典映像のDVDには彼らの“シングルプロジェクト”としてリリースされた最新ソロ曲4曲のMusic Videoを初収録。尚、このアルバムのためのジャケットデザインもデザイナー兼美術作家であるナオルが手掛けている。<br>
