《メンバーコメント》01.風の記憶2012年にリリースされた1st.ソロアルバムのリード曲。自伝的記憶の内容を主にした歌詞で、人間に関するメッセージを込めました。アレンジャーのミスで半音キーを上げレコーディングしてしまったのですが、よりアピール力が高まったのではないかと思います。<br>
02.My Girl1980年代のポップサウンドを基に作った曲で、伝説のバンドTotoのセッションサウンドを表現したかった曲です。ボーカルにはMichael Jacksonに対するオマージュが込められシンセサウンドが魅力的な曲です。<br>
03.同じ時の中の君2015年、BROWN EYED SOULのオリジナルアルバム発売前に、シングル・プロジェクトのトップバッターとして配信リリースされた、90年代に流行ったUrban R&amp;B。キーボード中心の構成にコーラスでサウンドを埋め、気楽に聴けるよう作った曲です。心地よいメロディーとは反対に切ない歌詞が聴きどころと言えるでしょう。<br>
04.別れの始まり長い時間を共にした恋人たちの別れの話を描いた曲で、最近ではあまりない起承転結がとてもよく表現された曲です。作曲に参加したアレンジャーとミュージシャンたちとスマートで明確な構成を引き出すことができた満足度が高い曲です。<br>
05.花よりも君がFeat. Simon D2012年、1st.ソロアルバムリリースに先駆け先行配信した曲。当時Supreme Team(ヒップホップ・デュオ)として活動していたSimon Dがラップ・フィーチャリングした曲で、ヒップホップシーンでは、稀に経験することのできない温かく愛らしい雰囲気を表現しようと努力しました。愛する恋人を想像し自ら作詞した曲という点に意味合いがあります。その人は現在僕の奥さんです。<br>
06.忘れるよ2012年にリリースした1st.ソロアルバム「Easy」のリード曲。Easy Listeningというジャンルで共に共感できる90’sポップバラード感を出したかった。別れを歌った歌詞とは真逆の温かいサウンドで、笑顔と涙を同時に宿すことができる、過去の恋愛へのノスタルジアを表現したかった。<br>
07.君のことだけ Feat. GaryBROWN EYED SOUL　シングルプロジェクトにて2番目に配信リリースされた曲で、片思いの胸騒ぎをイメージして作った曲です。Leessang(ヒップホップ・デュオ)のGaryがラップ・フィーチャリングした曲。軽く揺れるリズムに体をゆだね、来る春をイメージするに好ましい曲。やはり春にリリースされた。<br>
08.YouBROWN EYED SOUL 3rdオリジナルアルバムに収録されたソロ曲でホーンセクション・サウンドを中心に温かい雰囲気を醸し出そうとした。アルバム全体のバランスを考えアレンジしました。<br>
09.どれだけSong Ver.2012年にリリースされたシングル曲。これまでやってきたメロディー変形の多い生粋のブラック・ミュージックとはまた違うニュアンスを出したく、明確なメロディーラインとドラマティックなメロディー展開を中心にアレンジしました。シンプルで穏やかなピアノと豊かなサウンドの対比を中心に聴いて頂ければと思います。<br>
10.A Song For You2011年にリリースされた1st.ソロアルバムの収録曲。Jazzyな雰囲気とそれに合いまった歌い方で曲の雰囲気を最大限に表現しました。ディープなピアノとボーカルの上で、軽やかにそよぐギター・サウンドでユニークさを表現しようとしました。<br>
11.君を愛してるBROWN EYED SOUL シングルプロジェクトにて3番目に配信リリースされた曲。約3年越しに作業していた未完成の曲だったのですが、ある日突然サビ部分が思いつき、それから6ヵ月で完成に至ったというエピソードがあります。シングルプロジェクトのコンセプトによくあった曲ですね。<br>
12.With ChocolateBROEN EYED SOUL 3rdオリジナルアルバムに収録されたソロ曲で、人気が高い曲。モダンR&amp;B調に自由なスキャットを入れ込み、聞き手を飽きさせないように作ってみました。<br>
13.Come With Me GirlBROWN EYED SOUL シングルプロジェクトの4番目に配信リリースされた曲で、過去の恋の思い出を振り返るようなわびしさを込めた。作曲は事前に進めていた曲で、行きつけのフラワーショップにある水仙を見て、歌詞を完成させた曲。<br>
14.春の日2008年にリリースした1st.ソロアルバム「Thinkin’ Back On Me」の収録曲で、タイトルのように暖かい春の日のような元恋人の暖かさを表現しようと努めました。エピソードといえば、現在人気のある女優に提供する予定であった曲だったということです。<br>
15.My Valentine2015年にリリースされた3rd.ソロアルバム「Merry Go Round」のダブルリード曲のうちの一曲。誰かに出会い恋をしたらこんな気持ちではないかと想像し書いた曲で、その雰囲気に合った明るく甘い歌詞をつけてみました。ライブでギターを弾きながら歌うと無性に気分が良くなる曲。<br>
16.Nothing betterBROWN EYED SOULの”ジョンヨプ”を多くの方々に知ってもらうきっかけとなったありがたい曲。ピアノと歌声2つであふれんばかりの愛の感情を表現したく作った曲で、制作にとりかかってから10分でメロディーと歌詞ができあがってしまった不思議な曲。<br>
 
