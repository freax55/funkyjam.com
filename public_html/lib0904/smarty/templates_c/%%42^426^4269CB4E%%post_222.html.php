<font color="#FFCC00">名古屋暑いんですけど…</font><br />
<img src="/images/common/dot.gif" width="1" height="8" /><br />
<br>
･<a href="/artist/urashima/diary/image/20131008_2.jpg"> 画像はｺﾁﾗ </a><br>
東京の友人から、寒いね〜〜とメールを頂きましたが、名古屋は連日暑いんですよ。あれ〜〜？こんなだっけ…レミで行くとこ行くとこ暑いんですよね。。。でも私の名古屋公演は着々と千龝楽に近づきつつあります。<br>
<br>
先週末、中日文化センターで講義に参加させていただきました。<br>
旦那さん役の駒田一さんと一緒に、評論家の扇田先生のクラスに招かれてのことでした。<br>
子供をいびるおばさんのマダム…現代で観ると違和感あるけど、この時代のフランスには、こうゆう貧しい層の階級の人間が多く、その中では、この関係はさほど珍しい事ではなかったという話をすると、スリランカから小学生の娘さんとご一緒に講義に参加してくださっていた方が、「スリランカでは、子供を働かせるのはごく一般的に行われていて、生活レベルも国民意識も低い・・その中でテナルディエのような考え方の人はいっぱいいます。時代と国の背景の中で人間の価値観は大きく異なるんです。」というようなお言葉を頂いて、こちらも大変勉強になった素敵な時間でした。。。一ちゃんの暴露話も面白かったし！また機会があったらやりたいなぁ〜〜司会者が私じゃなく、進行役がいての話ですよ。。そこんとこよろしくです。はい。。<br>
<br>
写真は駒田ハジメちゃんおすすめ、すがきやのラーメン！ 大須観音をお参りしてからの流れなつかしいでぇす。 <br>

