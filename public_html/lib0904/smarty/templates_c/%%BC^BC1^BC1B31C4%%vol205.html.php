キリバス共和国から戻ってまいりました。<br />
その体験を、これから報告致します。<br />
ほとんどの原稿は現地で書きました。<br />
出来る限り正確かつワタル的にをモットーに、ワタル自身inキリバスを楽しんで頂けたら、無上のシアワセでありますm(_ _)m<br />
<br />
<br />
5月21日(水) 快晴<br />
仁川(韓国)経由でナンディ(フィジー)に午前8時30分到着。トランジットのため1泊。<br />
空港近くのモーテルにチェックインすると、さっそくパンツを脱いで外出。<br />
あのね、ノーパンといっても、まさか下半身マル出しじゃないからね。短パンの下にパンツを穿いてないってコトだからね。<br />
え!?  どーしてノーパンになる必要があるかって?<br />
あのね、極上の解放感を味わえるんだよ。特にオトコはね。<br />
オレ、剣道をやってたんだけど、剣道着の袴(はかま)の下って、ノーパンなんだよ。<br />
それで小手ェとか胴ゥとかメェェェンとかやると、オトコは「揺れる」のよぉ←オギママ風にね^^;<br />
その揺れが、たまらない解放感なの。<br />
だから、ノーパンなのにジーンズとか履いちゃうと、揺れないから解放されない。ノーパンの醍醐味は、ダブダブの短パンとかジャージじゃないと発揮されないんだよ。<br />
NPOの全国ノーパン愛好同盟=略して全ノー愛同は、これを「振り子の原理」と呼び、露出狂のメンタルケアへの応用を模索している←ウソよぉ←オギママ風だからね^ ^<br />
えー、ノーパン談義はこのくらいにして、宿の隣に大きなスーパーマーケットがあったり、ガイドブックに載っていたインド系レストランが至近距離にあったりして、思いのほかに快適なトランジットだ。<br />
特に、インド系のヤギ肉フィジーカレーは、ワタル人生で5本の指に入る美味で、早くも期待度150%のキリバスを予感させる。<br />
<br />
しか〜し!<br />
宿のシャワーはチョロチョロっとしか出なかったり、夕食のホットドッグはヨーマネーズ=マヨネーズベチャベチャで全然ホットじゃなかったり…。<br />
<br />
しか〜し!<br />
はじめて見上げる空の広さと空気の鮮度に、はやくも全身が高揚していた。<br />
フィジーDaysは後日に書くかもしれないとして、その夜は早々に就寝。<br />
<br />
しか〜し!<br />
なんとも恐ろしーコトに、その夜の夢に、ファンキー・ジャムの大森[美人]社長が現れたのだ!<br />
「なんでシャワーのお湯が出ないんだよッ!? 」と怒り爆発で、オレの欠点を10も20も並べたて、「だからワタルさんはダメなのよ!」と説教されつつ朝5時に目が覚め、にわかに暗雲aboveキリバスの予感となった(・・;)<br />
<br />
<br />
5月22日(木) 快晴<br />
大森社長のナイトメアも杞憂だったのか、予定通り朝8時発のフィジー航空で、快晴の太平洋をタラワ(キリバス)へ飛び立つ。<br />
人生初のノーパンon旅客機で、身体チェックのハプニングを期待するも、ゲートでブブーも鳴らずに無事通過。<br />
キリバスに近づくと、眼下の島は、これまで見たコトもないブルーのグラデーションに包まれ、海底の岩礁までクッキリと見える。<br />
「スゲ〜!」<br />
思わず声になり、旅のテンションは垂直に跳ね上がる。<br />
3時間の飛行で降り立ったタラワ空港の、入国管理センターが木造平屋なのも嬉しかった。<br />
入国手続きもボールペンとスタンプだけで、荷物を受け取るターンテーブルもなく、スタッフが木の台に荷物をどんどん置いてゆく。<br />
無事に入国審査を終えて管理棟を出ると、国際空港の外には客待ちピーポーと、柵にしがみついた子供達と、水たまりの広場に売店小屋があるだけ。<br />
<br />
しか〜し!
インターネットで予約したロッジのスタッフが迎えに来ているハズ。<br />
<br />
しか〜し!<br />
フツーは予約客の名前を書いたカードとかを掲げているのに、それらしき人物が見当たらない。<br />
仕方ないので、いかにも困っている旅行者のポーズで待っていると、それでもダレもナンにも言って来ないので、IDパスを下げた女性に「ラグーン・ブリーズ・ロッジへ行きたいんだけど…」とゆーと、人ゴミの中から赤い花柄のワンピースを着た小柄な女性を呼んでくれ、「彼女がロッジのスタッフよ」。<br />
言われて見ると、彼女が手にしているA5くらいの紙に、小さな文字でナニやら書いてある。<br />
花柄ワンピースは恐縮しているよーで、「気づかなくてすみません。アナタ、この方ですよね」と、その紙の文字を指さす。<br />
<br />
しか〜し!<br />
そのボールペン手書きの筆記体が迷路みたいにグンニャグンニャで、どーしてもWATARUらしき文字を見つけられない。<br />
「オレはワタル。この人じゃない。予約もしてあるよ」と、iPhoneに保存してある予約画面を見せた。<br />
実は、予約して数日後にロッジから「予約日にはキリバス着の飛行機はないが、本当に来るのか?」とゆー英文メールが届いたのだ。<br />
そこで、かつてカナダ留学中に強烈体臭フレンチ男とDay&Nightし、英語堪能なファンキー・ジャムのナリカワ女史に頼み、「エアチケットの予約は完了しているけど、本当に当日便がないなら日本で再確認してみる」と返信してもらった。<br />
すると、「ゴメン。こっちが日程を間違えてたよ。当日はキリバス着の便があるから、ワタルの到着を待ってるからね」と戻って来たのである。<br />
なのに花柄ワンピースは、オレじゃなくて別人を待っていたのだ。<br />
それでも“神器”iPhoneの登場で納得したのか、「予約の人がまだ1人来るから、ちょっと待ってて下さい」と、よーやく送迎車のトヨタ・カリーナに乗せてもらえた。<br />
<br />
しか〜し!<br />
こーゆートラブルも独り旅の楽しさで、ハプニングやアクシデントを受け入れられなくちゃ、独り旅は楽しめない。<br />
<br />
しか〜し!<br />
のちに、ワタル自身もまだまだ未熟だと痛感させられるのだが、その時は久々の独り旅に浮き足立ち、ココロも浮いていたのだった。<br />
到着から20分後、A5筆記体ネームの予約客は現れず、ワタル自身はスタッフの運転でロッジへ向かった。<br />
フィジーでも驚いたが、キリバスの海と空と空気と緑の濃度はケタ違いで、オレが知っている世界とは、まったくの別世界だった。<br />
「スゲ〜島に来ちゃったな〜」<br />
もー完全に独り阿波踊り状態のまま、ロッジの受付に着いた。<br />
すると花柄ワンピースが、薄暗い教壇のよーな受付に立ち、A5サイズのノート=空港で持っていたネームカードと同じ紙に、やはり同じ手書き筆記体で「name/arrival/departure」と書き、オレに差し出した。<br />
えっ!?  コレってどーゆーコト?<br />
さすがにコレには戸惑った。<br />
フツーは最初に、パスポートや予約シートの確認があるのに、無地のノートに手書きって…!? <br />
そーだ、そーなのだよ。<br />
結局、ワタル自身の予約は受け付けられていなかったのだ。<br />
ファンキー・ジャムのナリカワをわずらわせてメールで確認もしたのに、まったく意味がなかったのである(・・;)<br />
で、手書きノートに名前と到着日と出発日を書き込んで、パスポートを見せろともナンとも言われず、宿泊料金さえ確認されず、2階の角部屋(4部屋しかないから全室角部屋なんだけど)に案内された。<br />
不安は増大した。<br />
どんな宿でも、宿泊料は最初に確認されるし、前払や保証金も異例じゃない。<br />
<br />
しか〜し!<br />
パスポートすら確認しないのが、キリバス流みたいなのだ。<br />
<br />
しか〜し!
やっぱり不安なワタル自身は、もー1度iPhoneで宿泊料7泊分明記の予約受領書を見せて、「これで大丈夫なの？」と訊いた。<br />
花ワンピは画面を斜めに見て、なんだか不服そーに「OK」と答え、部屋の鍵とピンク色のトイレットペーパー1本と赤い石鹸1個をオレに渡した。<br />
こーしてよーやくワタル自身inキリバスがスタートするのだが、ビューティホーな景色とはウラハラに、キリバスの旅は過酷な日々となるのだった…。<br />
