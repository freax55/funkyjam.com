<font color="#FFCC00">森大輔 ニューアルバム「Music Diner」好評発売中！</font><br />
<img src="/images/common/dot.gif" width="1" height="8" /><br />
 抜群のポップセンスと歌唱力、上質な音作りを生み出すマルチプレーヤー森大輔のニュー・アルバムが遂に登場！MISIA, EXILE ATSUSHI、SMAPなど、錚々たるアーティストにその力を買われ作品を提供する傍ら、温めてきた自作は最高級J-Pop全12曲！<br>
前作「Beauty is yours」より約2年半ぶりとなる新作は、更にアーティスト森大輔の個性と色気が光る作品となっている。2014年夏からスタートしたスペシャル・ライブ「森の音楽会」は回を重ねるごとに彼の音楽の奥深さと楽しさが届けられ、回ごとのテーマに特化して制作された曲（「フルムーン」「ごめんねと言って」「monologue」など）は見事な仕上がりとなっており、満を持してパッケージ収録となる。さらに森大輔パーソナリティ・ラジオ番組「NIGHT AQUARIUM」（FM COCOLO）のテーマソング、他アーティストに提供した楽曲のセルフカバー曲、それぞれ様々な表情を見せてくれる新曲も加えられ、これ以上ないほどお得感あふれるポップアルバム！<br>
