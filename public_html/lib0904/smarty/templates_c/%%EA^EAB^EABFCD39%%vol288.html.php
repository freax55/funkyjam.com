今年は暖冬などと言われているが、ゴム手袋を二重にしても冷水は指先を貫き、徹底的にフリーズさせる。<br />
作業が終わり、感覚がなくなったカチンコチンの指先に生気が戻る時が、コレまた拷問だ。<br />
ムズムズチリチリゾワゾワカリカリが指からヒジから肩から後頭部に広がり、両手を高速でこすり合わせてウナギ悶えするばかり。<br />
