ワタル自身が通っている某スーパーなど、なんと、普段は見切り品が置かれている棚がラーメン売り場にされていた。<br />
いくらなんでもそりゃねぇぜ…とココロを痛めつつ、大好きなマルちゃんの「昔ながらの中華そば〜お鍋だけで簡単調理!」を2袋ゲットでヒト安心(^.^)<br />
<br />
しか〜し!<br />
