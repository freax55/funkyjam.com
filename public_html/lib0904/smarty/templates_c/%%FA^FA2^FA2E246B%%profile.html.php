森大輔（もりだいすけ）<br />
<br />
1981年12月10日、大阪府生まれ<br />
</center>
<img src="../../images/common/dot.gif" width="1" height="8" /><br />
３歳よりクラシックピアノを始め、以後大学の音楽科を卒業するまで、間断無くピアノテクニックを磨き続ける。中学時代にポップス・ミュージックに目覚め、作曲を始める。<br>
				基からの素養に加え、高度な音楽的センスを更に極めながら、独自の音楽世界を構築する。<br>
				耳馴染みの良い音作りでありながら、プロ・ミュージシャンをもうならせるテクニックを忍ばせ、デビュー当時から熟成された音楽性に、玄人・素人の区別なく賞賛を得る。<br>
				作詞・作曲・編曲すべてをこなす天才肌のアーティストである。<br>
				また無類の職人気質も備えており、他アーティストへの楽曲・編曲提供の際、それぞれのアーティスト達の才能や個性を理解し、新境地を開く事のできる能力に、常に驚愕と感嘆をもって迎えられている。<br>
				<br>
				品性を備えた人柄でありながら、大阪生まれならではのユーモアと機微溢れる語り口は多くの人々に愛され、健康的でありながらそこはかとなく漂う色気と甘い歌声は女性ファンを、音楽性の高さにおいては男性ファンを、作品力ではその両方の心をつかみ続けている。<br>
				昨年リリースしたアルバム「Beauty is yours」を掲げた全国ツーアは大盛況のもと幕を閉じた。<br>
				また、同時に久保田利伸野外ライブツアーにキーボード＆コーラスとして出演。<br>
				今夏より企画ライブ「森の音楽会」をスタート。大成功を収めた第一回は“雨”をテーマに、第二回は“月”をテーマに、第三回は“愛”をテーマに開催。「森の音楽会」は今後も断続的に開催し、同時にテーマをモチーフにした新曲も披露し続け、更なる発展を遂げている。
