ワタル的諸事情により3月はバイト強化月間で、4月は久保田利伸FC会報編集月間になる。<br />
例年ならゴールデンウィーク明けが編集締切なのに、今年の締切は連休前で、しかも、今まだ久保田の取材日程も未定で、前号につづき激しくイヤな予感がする(･･;)<br />
そんな穏やかならぬ日々の間隙を縫い、独り花見by流星号を敢行した。<br />
目的地は、東京の西部にある小金井公園と野川公園。どちらも桜の名所で、特に野川公園は緑の中を小川が流れ、絵のよーな景色にココロ洗われる名園だ。<br />
当日は晴天だけど風は冷たいデブ日和、快適なペダリングで走っていると、あちこちで、桜に携帯電話のカメラを向ける人を見かける。<br />
ワタルの初カメラは、小学生の時に父からもらった、アサヒペンタックスの一眼レフ。ピントも露出もマニュアルの、ねじ込み式レンズのフィルムカメラだ。<br />
父は、当時の最高機種といわれたニコンFのファインダー違いを2台も持っていたカメラマニアで、小学生のガキに絞りとシャッター速度の関係なんかを分かりやすく教えてくれた。<br />
以来、今も写真を撮りつづけているが、デジカメの進化で、ホントーに便利になった。<br />
簡単便利はメカ苦手世代にとって大きな魅力だから、最近は高年齢ピーポーが一眼レフのデジカメで、草花や野鳥を撮影している姿を、しょっちゅー見かける。<br />
撮ったその場で、自分の写真を確認出来るデジカメは、手軽な趣味になったんだと思う。<br />
レンズも高性能のオートフォーカス・ズームレンズが安価になり、重たい単焦点レンズを何本も持ち歩く苦労がなくなったのも、高齢者にはアリガタイ。<br />
あのね、今だから白状するけど、久保田の仕事で海外へ行く時、重たいカメラ機材がホントーに苦痛だったの。<br />
その反動で、プライベート旅行にはカメラを持って行かない時期が、しばらくあった。<br />
