えっ?!<br />
一眼レフってナンだってか?<br />
えー、そーゆー人はネット検索にお任せてして、小金井公園へ向かいまーす。<br />
到着したのは11時前、平日だからか子ども連れのママが多く、鳩を追いかけ石を投げつけているガキが2人もいた。<br />
足を引っかけて転ばせてやろーと思ったが、そのママが意外に美人だったので、優しー笑顔で悪ガキを見守る。<br />
美人ママとはナニゴトも起こりそーにもないので、東八道路に出て野川公園へ。<br />
前記したよーに、ここはホントーに大好きな場所だ。<br />
バーベキュースペースもあり、これからの休日は賑やかになるが、東八道路をまたいだ広々とした緑は、ゆるやかに背筋を伸ばし、ココロを癒してくれる。<br />
桜の下のベンチで、ワタルmadeのお花見弁当タイムにする。<br />
最近ハマっているマルハニチロの冷凍食品「いわしの梅しそはさみ揚げ/天つゆがけ」を、バイト弁当には1個のところ、お花見スペシャルで3個の大胆投入!<br /> さらに、これまたハマっている永谷園「おとなのソフトふりかけ/鶏そぼろ」を具にした卵焼きと、自慢のワタルチャーシュー&去年漬けたワタル梅干し!<br />
ナンだカンだ言ったけど、この弁当をね、桜を愛でながら、楽しみたかったのよ^o^<br />
