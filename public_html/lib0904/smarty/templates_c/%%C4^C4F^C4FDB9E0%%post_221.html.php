<font color="#FFCC00">名古屋〜〜！！</font><br />
<img src="/images/common/dot.gif" width="1" height="8" /><br />
レ・ミゼラブル名古屋公演始まりま〜〜す！！なので名古屋におりますが・・季節は移ろいすでに秋の気配。。<br>
素敵なこの時期、着るもののチョイスがめちゃ難しい〜&#128166;<br>
キャストのある子は、大阪にノースリーブばかり持って行ってしまって、寒かった〜と言うておりました。<br>
わたくしも、名古屋入りの朝、東京は今シーズン一番の冷え込み・・さっむーっつってヒートテック出して着たわええが、５分で脱ぎましたわ。<br>
もうわけわからん感じでね・・正直行った先々で服買ってます。。<br>
名古屋は、関東ほどの冷え込みはないんですが、夏物っつー訳にはいかんのがつらいところ。<br>
<br>
以前、NYで合いものの一重のコートを探しました。昔、メイシーズで見つけたので、再購入しようとデパートを廻れど無い〜〜！<br>
よくよく聞けば、NYには合いの季節は無いと・・確かに１０月から１１月に行ってましたが、ずーっと暑いまま・・夏モン着ててある朝、ぐっと冷え込み、吐く息が真っ白になって、みんなコートや手袋をしだすという、カットアウトみたいな季節変化を体感してべっくり！！<br>
日本は四季があっていいなぁ〜と改めて思ったものの、やはり着るものは難しい〜〜（泣）<br>
&nbsp;<br>
<br>
･<a href="/artist/urashima/diary/image/20131002_2.jpg"> 画像はｺﾁﾗ </a><br>
そんな名古屋の中日劇場が大阪と打って変わって古くて狭い！<br>
それだけ歴史ある劇場で、たくさんの演者の汗と涙が詰まっているという事です！！<br>
大所帯のレミは、ありとあらゆる方法でスペースを確保せねばならず大変ですが、子役ちゃんたちと、楽屋が近くなって私は嬉しい！！<br>
前半組の前列左から、まいまい、詩音、青空、私のとなりは、くるみぃ〜〜！！ぐへへ、楽しいぃぃ〜！！ <br>

