ところ狭しとテーブルが置かれた店は、バレーコート４面分くらいの大スペース。<br />
それを取り囲むよーにズラリと並べられた水槽には生きた魚や貝やカニやエビがギッシリで、蒸すだ焼くだ炒めるだ刺身だナンだカンだと、好きな食材を好きな方法で調理してくれる海鮮レストランは、まるで東京築地の場内で食事をしているみたい。<br />
しかも、この時期は中国の旧正月=春節で、レストラン全体が大晦日のアメ横さながらに沸騰し興奮し混乱し動転し、ジャパニーズinチャイニーズには辛抱の極限が求められる。<br />
えー、このあたりを詳しく書くと、キャノピー・ウォークにたどり着けないので、そのうち「ヴィジュアル系ワタル自身」で写真紹介します。<br />
で、キナバル国立公園だ。<br />
