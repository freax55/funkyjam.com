つつつ冷たい…けど気持ちイイ…けど痛い…あらら…本当に魚が寄って来た…黒くて小さい魚が5匹、10匹、20匹…モゾモゾする…けど気持ちイイ…けど小指が痛い…（≧∇≦）<br />
