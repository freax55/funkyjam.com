マレーシアのコタキナバル(Kota Kinabalu)は、ボルネオ島の南西に位置するサバ州の州都だ。<br />
サバ州観光の目玉は、アジアのアマゾンとも称されるボルネオの大密林と、そこに棲むオランウータンなどの希少な野生動物、そして魚が群れる珊瑚礁の海だ。<br />
ヨースルに、自然大好きピーポーなら歓喜雀躍の場所で、ワタル自身も今回は海を離れ、ジャングルに分け入ってオランウータンを見たいと思っていた。<br />
<br />
しか〜し!<br />
オランウータンの保護地区まではコタキナバルから国内線に乗り継がなければならず、今回は日程も予算も不如意のため、コタキナバルから日帰り可能な「世界遺産キナバル国立公園ツアー」に参加した。<br />
キナバル公園は富士山より高いキナバル山(4095.2m)の麓に広がるジャングルで、植物園やプールみたいな温泉、トレッキングコースなどが整備されている。<br />
中でも、ワタル自身が絶対にやりたかったのが、キャノピー・ウォークだ。<br />
キャノピーとは、ジャングルに自生するメチャ高い樹から樹へ架けられた吊り橋で、一番高い所では地上30メートル以上にもなる。<br />
ロープで編まれたグラグラユラユラの吊り橋を渡りつつ、上から目線で密林を見渡すのが、キャノピー・ウォークの醍醐味なのだ。<br />
吊り橋は数本の樹々で中継され、ジャングルの樹上をジグザグに縫い、全長150mにもなる。<br />
これは絶対やってみたい!<br />
鬱蒼とした熱帯のジャングルを、地上30mの吊り橋から見下ろしてみたい!<br />
なにしろ日本の過激な世界遺産、縄文杉in屋久島を制した男=ワタル自身なのだ。<br />
ボルネオの世界遺産とはいえ、オチャノコサイサイじゃ^o^<br />
