日本のバラエティ番組なんかでも紹介されたから、知っている方も多いと思う。<br />
その医者魚に、疲れたデブ族の美味なる足を味あわせてあげ、自らも癒されたいとゆー思案。<br />
で、滝壺に着いて靴を脱ぎ、靴下を脱ぎ、いきなり左足の小指を岩で強打した!<br />
