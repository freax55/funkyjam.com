しか〜し!<br />
いくら登ってもキャノピー・ウォークに到着しない。<br />
ゼェゼェゼェと息が上がり、デブ族の弱点=ヒザが悲鳴を発する。<br />
なな…舐めていた…ボルネオの本格的なジャングルを、舐めていた…!＞＜!<br />
