すると、受付カウンターは23時で閉まるから、カギをWATCHMANに預けておくので勝手に入って…の返信で、今夜は夕飯も酒もガマンの覚悟を決める。<br />
深夜1時過ぎ、ヘロヘロになって到着したシャレーは、ビビアンと2人で1泊+朝食=7000円の激チープだが、シャワーもトイレも「普通」で、大安堵しつつ早々に就寝。<br />
昨夜は暗くて分からなかったが、翌朝カーテンを開けるとヤシの木に囲まれた芝生の庭に、小さいながらもプールがあり、それを取り囲むよーに数棟のシャレーが建てられ、リゾート気分ノープロブレム。<br />
朝食ビュッフェは普通以下だったけれど、2階のテラスから眺める景色が素晴らしく、ありがたくトーストをいただく。<br />
