週末毎にドリカムブッチ切りLIVE「Attack25」を各地で繰り広げておりんすが私には6代目になる愛猫がおりまして、ツアーの間そのコはどしてんの？とよく質問が。なのでお答えしましょう。愛猫の正式名称は福丸子→福の字を◯で囲む(でもそんな変換は無いの)それに子。お家でのお留守番は2泊まで。自動エサ出てくるマシーンが1日に3回ブイィンっつってご飯出す。お水も飲んだらその分足されますゥってヤツ。トイレは砂を多めに入れて、そこが落ち着くって感じにしておく。トイレ以外でされて可愛い愛猫に危険な感情がわく瞬間を経験してからはこうしてますん♪気温が上がる夏場と、2泊以上の旅の時は神奈川県座間市にある「さとう動物病院」にお泊まり。長いスパンだと2ヶ月なんて事もあり。猫も人も寂しいの我慢大会。さとう先生はペーペーの貧乏アマチュア時代からのお付き合い。エサあげちゃった野良猫の風邪薬を買いに行って100錠1000円で売ってくれてから26年。ズーーっとお世話になりっ放し。ありがたや。<br>
このツアーはお家での留守番が多い。静岡エコパアリーナでのLIVEを終えて帰宅した次の日、ふと何やら訴えるギミな愛猫。なんや？どうした？かわえぇぇのおお♪ベットにゲロが吐いてあった。危険な感情 沸。<br>
 
