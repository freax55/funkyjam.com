狭い日本の関東と関西でさえ、これほどの違いがあるのだから、国や人種や宗教が違えば、齟齬と誤解が生まれるのも仕方ない。<br />
日本では道端のお地蔵さんに手を合わせる人を、よく見かける日常として受け入れている。<br />
