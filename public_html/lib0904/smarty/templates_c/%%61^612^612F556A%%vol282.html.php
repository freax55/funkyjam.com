今春、配下の者と結婚した関西嬢が、「やっぱり東京は違う!」と感嘆していた。<br />
ナニがあったのか訊くと、渋谷のカウンターだけの狭いヤキトリ屋に入ると、客の中に神田正輝さんがいて、シンゾーが止まるくらい驚いたとゆーのだ。<br />
さらに関西嬢が驚いたのは、神田さんの写メも撮らずサインも頼まず、平然とヤキトリを食べている一般ピーポーに、激しくガクゼンと「やっぱり東京は違う!」のカルチャーショックを受けたとゆーのである。<br />
「大阪やったら大騒ぎやで!」なのだそーだ。<br />
<br />
しか〜し!<br />
東京の一般ピーポーも、唐突に芸能人などに出くわすと、ワーキャー率がかなり高い。<br />
かつてプロマイド売上No.1になった男性アイドルの仕事をしていた時など、ソレはソレは大変マルな騒ぎで、取材に行くのが毎回苦行だった。<br />
そーいえば最近は「プロマイド」なんて言葉も死語になったのか、オリコンにも「プロマイド売上ランキング」など見当たらない。<br />
てゆーか、21世紀ピーポーはプロマイドを知っているのだろーか?<br />
今から25年前の1990年、宮沢りえちゃん17歳の「ふんどしカレンダー」を買いに、浅草のマルベル堂に駆けつけたワタル自身35歳…のマルベル堂がプロマイド撮影をしたら一人前の芸能人だと言われた時代は、デジタル時代と共に消え去ったのか…(￣O￣;)<br />
おっと、今回もまた話題が逸れた。<br />
芸能人ワーキャー問題だった。<br />
<br />
しか〜し!<br />
みんなに嫌われるのを覚悟でゆーが、ワタル自身は芸能人や有名人などの写真を勝手に撮りまくる、写メピーポーが大嫌いだ。<br />
芸能人本人が写メOKだとしても、「撮ったら負け」の精神を理解していない、一般ピーポーの「一般」部分が哀しすぎるのだ。<br />
<br />
しか〜し!<br />
ワタル自身も、もしもローラちゃんと一緒に写メしてもらえるなら、大喜びで負けを認め、「ローラちゃんとツーショットしちゃったもんね!」と自慢しまくるコト確実である。<br />
<br />
しか〜し!<br />
現実問題として、全国的に有名な芸能人などの街中含有率は関西より東京のほーが高いので、東京人は「偶然芸能人」の免疫を少なからず持っているのかもしれない。<br />
神田正輝さんがいらしたヤキトリ屋はワタル自身もたまに行くが、ほとんど常連客ばかりなので、神田さんが来店した瞬間に「騒いだら負け」の東京免疫が伝播したのだ。<br />
ワタルが常連の渋谷Mにも多くの芸能人が来られるが、一瞬「オッ!」と思ったとしても、客は見て見ないフリをし、芸能人領域には接近しない。<br />
それが東京の「騒いだら負け」美意識なのだ。<br />
<br />
しか〜し!<br />
