この前、深夜にロポンギに出かけた。大人の女、ロポンギにも行く。JAZZBARでジャムセッションナイト。小さなクラブにだんだん人が集まる。セッションが始まる。テンポやコードもその場で伝えながらソロ回し。2、3曲でメンバーも客(ここはあえて客と呼ぶのが近い感じw)も温まって来る。客席から呼び込まれたゲストがマイクを握る。スキャットが始まる。リズムを刻み出すと全体がうねり出す。すゲェ。一曲終わるとチビリと喉に酒を通す。そしてもう一曲。からだを揺らして見ていた客が今夜のラッキーを見落とさないよう、コードと絡んで編まれる声を聞き逃さないよう釘ずけになる。曲がエンディングに入った。音が止まる。マイクをおいたその人が久保田利伸。その夜の全てに感謝。<br>
