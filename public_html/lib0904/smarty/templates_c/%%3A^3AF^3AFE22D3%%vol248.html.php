4月1日、東池袋の人気ラーメン店「大勝軒」の創業者=山岸一雄氏が80歳で亡くなった。<br />
戦後すぐにラーメン店で働きはじめた氏は、まさに人生をラーメンに捧げた人物で、最近流行の「つけ麺」を開発したとして知られている。<br />
ラーメンの味ばかりでなく、優しく思いやりのある人柄は多くの人を惹きつけ、100人以上もの弟子にノレン分けをして大勝軒の味を国内外に広めたが、長年の厨房生活で身体を酷使し、病気とも縁の切れない氏であった。<br />
ワタル自身もラーメン屋をしていたから言えるが、ラーメン屋は大変マルな重労働だ。<br />
調理に使うズンドウは巨大で重たいし、火力全開の厨房は熱いし、開店の数時間前から当日の仕込みにとりかからなければならないし、立ちっぱなしの営業後には清掃と翌日の準備が待っている。<br />
<br />
しか〜し!<br />
本社から届けられた冷凍スープを暖め、具材を解凍するよーな店は論外。刻んだネギまでパックで提供される店も多く、包丁やマナ板がなくても営業出来るチェーン店さえある。<br />
そんな店をやって楽しいのかと思うのだが、そんな店をやる人は利益だけが重要で、ラーメン作りには興味がないのだ。<br />
ワタル自身にとってラーメン屋の至福は、自分の味を追求するプロセスに尽きる。<br />
スープを煮出す食材のバランス、タレに使う醤油の配合、香味油の濃淡、麺の太さと小麦の種類、チャーシューの部位や産地などなど、悩むコトが多ければ多いほどラーメン作りは楽しくなり、美味しいラーメンを作ろーとすればするほど利益率は急降下する（≧∇≦）<br />
だから、よーやく完成させたラーメンをお客さんに「ウマイ!」と言われる瞬間が至福そのもので、それ以外は肉体労働ばかりで楽しいコトなどない。<br />
<br />
しか〜し!<br />
でもまぁ、ラーメン店に限らず、多くのチェーン店の安価な食事が、日本の労働者を支えているのだから、けして否定は出来ないのである。<br />
かつてのラーメン屋は、トーチャン・カーチャン商売と呼ばれていた。<br />
ラーメン屋のよーな薄利商売は、夫婦が自宅で経営するのが適当な規模だとゆー意味だ。<br />
<br />
しか〜し!<br />
行列が出来るラーメン屋ブームの到来で、経営者がオクマンチョージャになったりし、ラーメンでイッパツ当てたら大儲けとなって、フレンチ・シェフのラーメンだとか、和の達人によるラーメン懐石だとか、イタリアンなつけ麺だとか、ラーメンがラーメンから遠くなってしまい、トーチャン・カーチャンの出る幕はなくなってしまった。<br />
今やラーメンはナンデモアリ状態だけど、その中でもワタル自身がイカガナモノカと思うのは、「和風ラーメン」と掲げている店だ。<br />
そもそもラーメンは和風だっちゅーの!　ラーメンは日本オリジナルだっちゅーの!<br />
そーゆー意味でも、ラーメンの王道を貫きながら、つけ麺を生んだ山岸氏は偉大なレジェンドなのである。<br />
<br />
しか〜し!<br />
今これを読んでいる皆さんには、もーひとりのラーメン・レジェンド=故・大宮守人氏を記憶にとどめて頂きたい。<br />
日本のポップス音楽界に、before久保田利伸/after久保田利伸の境界線があるよーに、ラーメン界にもbefore大宮守人/after大宮守人のボーダーがある。<br />
札幌で「味の三平」とゆーラーメン店を開店した大宮守人氏こそ、ラーメン界の産業革命=味噌ラーメンを発明した大人物なのだ。<br />
今では誰もが普通に食べる味噌ラーメンだが、ラーメンのスープに合う味噌、具材、麺…すべてを完成させたのが、氏なのである。<br />
特に、西山製麺と共に開発した多加水麺は画期的で、その後につづく様々なラーメンに多大な影響を与えた。<br />
ラーメンの麺には大きく分けて、多加水麺と低加水麺の2種類がある。<br />
大宮氏&西山製麺が共同開発した多加水麺は文字通り水分を多く含んだ麺で、ちぢれていてコシが強く伸びにくい麺で、熱々濃厚な味噌スープに最適なのだ。<br />
逆に低加水麺は水分が少なく、比較的やわらかい麺でスープが染み込みやすく、繊細な味のスープと相性が良い。<br />
久保田利伸のFM番組収録で札幌へ行った時、ワタル自身の強烈なリクエストで「味の三平」を訪れた。<br />
現在の「味の三平」は二代目が受け継ぎ、ビルの4階で営業しているが、老舗の味噌ラーメンは健在だった。<br />
久保田いわく、R&Bは常に時代の最先端の音楽だが、開祖は堂々たるオールドスタイルで、動じぬ自信と意地を漂わせていた。<br />
レジェンドは消えても、偉業は遺る。<br />
山岸一雄氏&大宮守人氏の2大レジェンドに、多くの感謝を込めて、献杯!<br />



