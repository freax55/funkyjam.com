<font color="#FFCC00">久保田利伸 New Single「声にできない」ミュージックビデオでは中国の国民的女優と共演！</font><br />
<img src="/images/common/dot.gif" width="1" height="8" /><br />
久保田利伸 New Single「声にできない」ミュージックビデオでは中国の国民的女優殷桃(イン・タオ)&lt;2009年NHK日中共同制作ドラマ「蒼穹の昴」出演&gt;と共演。<br />
上海での撮影を経て“叶わぬ恋”のひとつの形を具現化。作品の切なさにより彩りを与えている。<br />
<br /><br />
【共演者　<img src="../../../images/artist/kubota/text1.gif" />(イン・タオ)プロフィール】<br />
<center><img src="../../../images/artist/kubota/intao1.jpg" /></center>
1979年生まれ　中国重慶出身<br />
2003年演劇「我在天堂等<img src="../../../images/artist/kubota/text2.gif" />」（天国で待っている）で、第５回中国演劇金獅賞の演技賞を最年少で受賞。<br />
2009年日中共同制作ドラマ「蒼穹の昴」(NHK)チャン夫人役で出演。<br />
同年、ドラマ「楊貴妃秘史」の楊貴妃役を演じ、中国全土で見ていない人はいないという程のヒットを飛ばし高視聴率を得る。この作品で国民的女優に押し上げられる。<br />
2010年演劇「四世同堂」が台湾で大ブレイク。<br />
2011年10月開始のドラマ「武則天秘史」（武則天役）は今年一番の期待作と呼び声が高い。<br />
<br />
【上海での撮影】<br />
「声にできない」ミュージック・ビデオに、この曲の持つ切なさを表現してくれる女優を模索していたところ、あるテレビドラマに出演していた彼女を常々見ていた久保田自身が迷うことなく推薦。<br />
コンタクトを取ったところ、既に久保田利伸の音楽に好意を持っていた彼女は快諾。但し彼女は映画撮影の佳境を迎えており来日が叶わず、一度は諦めたものの、どうしても彼女に代わる人を見つけられず、意を決して中国での撮影に臨むことになる。<br />
映画撮影の隙間の中で、久保田始め日本制作サイドの思いを受け止めた彼女はどこまでもプロフェッショナルに期待以上の熱演をしている。<br />
