1994年にティム・バートン監督&ジョニー・デップ主演の映画「エド・ウッド」が公開されるまで、ワタル自身はエド・ウッドとゆー人物をまったく知らなかった。<br />
エド・ウッド(Edward Davis Wood, Junior/1924~78年)はハリウッドの映画スタジオで下働きをしながら、まったくヒットしない映画を作りつづけた奇才である。<br />
彼は超低予算&強行スケジュール下、わずか数日で70~90分の映画を完成させたと言われている。<br />
<br />
しか〜し!<br />
エド本人には自信作でも、あまりにアバウトな映画は観客に1ミリも受け入れられず、ヤケクソになってアル中になってしまう。<br />
<br />
しか〜し!<br />
映画館では見向きもされなかった「PLAN 9 FROM OUTER SPACE(プラン9・フロム・アウタースペース)」(1959年)とゆートンデモ作品を安く買い取ったTV局が、深夜枠の穴埋め的に何度も放送をしたら、これが一部のカルト映画ファンに支持(?)され、1976年に「史上最低の映画」と評価される&gt;_&lt;<br />
今はYouTubeでも「PLAN 9 FROM OUTER SPACE」は全編公開されているので、興味と無駄な時間がある方は是非とも観てちょーだい。<br />
あまりにも雑な編集、繰り返される資料映像とのツギハギ、学芸会レベルの演技とセット等は、さすがに「史上最低の映画」と感銘(?)せずにはいられない。<br />
ちなみにドラキュラっぽい役で登場する老人は、ベラ・ルゴシ(1882~1956年)とゆー俳優で、かつては何本もの映画でドラキュラ役を演じた大スター…日本で言ったら鞍馬天狗=嵐寛寿郎的ポジションの大御所である。<br />
ここで賢明な方はお気づきだろーが、ベラ・ルゴシは「PLAN 9 FROM OUTER SPACE」の完成前に亡くなっている。<br />
そのために落ち込んだエドのアル中は進み、映画製作が頓挫していた。<br />
<br />
しか〜し!<br />
もはや常人の域を脱していたエドは、ななんと、バプテスト教会を新たなスポンサーにするため、出演者全員を同教に入信させてしまう。<br />
それでも吸った揉んだ、いや、擦った揉んだあったのだが、バプテスト協会のイケメン牧師を映画に出演させたりでスポンサー契約を結び、ベラ・ルゴシの代役…明らかに髪の色が違う人物に顔を隠させてドラキュラを演じさせ、とうとう公開にこぎつけたのである。<br />
不屈のエド情熱! 恐るべきエド執念!<br />
<br />
しか〜し!<br />
常人ならば「なんでこんな最低の映画を観なきゃいけないの!?」の気持ちを抑えきなくなるので、最後まで観るには並々ならぬ覚悟と根性が必要だ。<br />
<br />
しか〜し!<br />
当然ながらエド本人は「史上最低の映画」とゆー評価に激しく不本意なまま、1978年に54歳の若さでこの世を去る。<br />
心血を注いだ映画でナニひとつ結果を残せず、アル中で死んでしまったエド・ウッドは、世間的なアングルで見ると確実に人生の敗北者だ。<br />
映画「エド・ウッド」を監督したティム・バートンも、エドの作品賛歌ではなく、映画に魅せられた男の一途な情熱…ほとんどクレイジーな彼の信念に焦点を当てている。<br />
<br />
しか〜し!<br />
ワタル自身は、エド・ウッドの人生がうらやましくてならない。<br />
アル中で死ぬほど大好きで苦しむモノがあるなんて、いつでも若い女性に恋をしているみたいで、本気でうらやましい。<br />
オレはアル中になれるほど、大事な世界を持ってないし、二日酔いの朝に向かい酒をする根性もない。<br />
実は数日前、今年で96歳になる母が倒れた。<br />
96なのだから…と言われたらソレマデなのだが、こんな息子でも、母には少しでも長く元気でいて欲しいのだ。<br />
正月に会った時は会話がちゃんと成立していたのに、救急病棟の母は、支離滅裂だった。<br />
言葉はハッキリしているのに、話の時間軸がメチャクチャなのだ。<br />
そして、壊れた世界の中で、なぜか母は自分自身を責めてばかりいた。<br />
もちろん、そんな母を見るのは初めてで、母が歩んできた長い人生の、闇を垣間見た気がした。<br />
<br />
しか〜し!<br />
