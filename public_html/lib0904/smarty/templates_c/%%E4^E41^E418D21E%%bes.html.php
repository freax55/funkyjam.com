*PROFILE*<br />
韓国で絶大なる人気+実力を備えた、最高のR&amp;B・ボーカルグループ「Brown Eyed Soul」(ブラウン・アイド・ソウル)が満を持して、遂に日本に進出決定。<br />
ナオル、ジョンヨプ、ヨンジュン、ソンフンの4人で構成されるBrown Eyed Soul。<br />
Brown Eyed Soul の原型であるBrown <br />Eyesのメインボーカルで韓国最高の歌手と認められたナオルを筆頭に、全員がメインボーカル、という歌の実力を立証されている奇跡のグループ。メンバーそれぞれが作詞・作曲を行い、ナオルに関してはアートワークまでも手掛けている。<br />
<br />
グループとしてプロモーション活動やテレビ出演等を一切行わないにも関わらず、必ずランキングは上位チャートに入り、アルバムの個々の楽曲が1位〜10位まで独占した記録も持つ。コンサートは一流人気アーティストのみに許された、韓国で最も大きな会場であるソウル・オリンピック公園体操競技場での開催が恒例であり、チケットがなかなか取れないという現象も度々生じている。<br />
<br />
ソロ活動においては、各々得意なジャンルや個性を活かしたソロ・アルバムをリリース。<br />
R&amp;Bのみならず、ポップスやジャズ、ダンス・ミュージック、ソウル、更には多くの人気ドラマのOSTを手掛けるなど、それぞれが多彩な才能を発揮している。<br />
メンバーひとりひとりが確立した人気を誇り、韓国のアイドル・グループの中で歌に秀でたメンバーがソロを取る際、選ぶのは彼等の楽曲である事が多く、若いアイドルから実力派アーティスト達まで幅広く憧れられているカリスマ的存在である。<br />
<br />
そんな実力派のBrown Eyed Soulが、満を持してavexより10月16日にミニアルバムのリリースが決定！<br />
Brown Eyed Soulの代表曲『ピキョジュルケ（邦題：さよなら）』や『Love Ballad』の日本語ver.と、彼らの実力が存分に発揮されているLIVE音源＋映像等が収録される予定で、CD+DVDとCDの2形態で発売される。<br />
