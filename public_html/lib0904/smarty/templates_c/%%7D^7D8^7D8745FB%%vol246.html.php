バイト仲間のロバートはデキちゃった婚で、1歳になる男の子がいる。<br />
彼が酒の席でグチをこぼした。<br />
奥さんは高い化粧品を買うのに、自分にはパチンコ代もケチる…と。<br />
実にどーも庶民の低レベルなグチだが、「だってね、コラーゲンとかナノ化粧品とか、あんなの全然効果ないんでしょ!?」と、ロバートは本気で不満なのだ。<br />
<br />
しか〜し!<br />
その夜のワタル自身はプレミアム・ホッピーでシタタカ酔っぱらい、あまりウンチクを傾けられなかった。<br />
ホントーは、こんな話をしたかったのだ。<br />
<br />
化粧をする女性の方はご存知だと思うが、すべての皮膚(表皮)は死んでいる。<br />
細胞の死骸が皮膚となり、生きている細胞を守っているのだ。<br />
人気モデルがカワイイとか、スター選手がカッコイイなんて言ってるけど、みんな死んだ細胞を見てるだけって考えると、実にマッタク複雑な気持ちになってしまう。<br />
男女のイトナミのおかげで、たった1個の細胞が分裂して2個になり、その2個がまた2個ずつに分裂して4個になり…わずか47回の分裂で、みんなの身体に必要な細胞の数=1京(けい)=1兆の1万倍になる。<br />
身体が完成し、無事に生まれて来たからといって、それで終わりじゃない。<br />
子供は背が伸び、歯が生えたり抜けたりまた生えたり、太ったり痩せたりする。<br />
すべては新陳代謝の結果だ。<br />
新陳代謝のスピードは、身体の部位によって違ってくる。<br />
健康な成人では、年中無休の心臓は働きつづけながらも20日くらいで細胞全部が入れ替わるし、飲んだり食べたりで残業もさせられる胃腸関係は、わずか5日程度のサイクルで「更新」される。<br />
さすがに骨は長くて3ヶ月くらいかかるけど、ツメや髪は自覚出来るほど素早く代謝する。<br />
そーしたバラバラの周期が全身的に1周するのが9年といわれ、誰もが細胞的には9歳のガキなのだ。<br />
ニワカには信じられないが、どーやら真実なのである。<br />
<br />
しか〜し!<br />
もっと不思議なコトがある。<br />
大事な大事な脳細胞は1時間に500個ずつ死に、どんどん新しい細胞にバトンタッチしながら、1ヶ月ほどで新品の脳ミソに生まれ替わるのだ。<br />
てコトは、ワタル自身は1年で12回もマッタク新しい脳ミソになっているのに、忘れたい記憶も残っているし、人格が激変して品行方正なオジサンになったりもしない。<br />
泥酔で記憶をなくすワタル自身でも、新陳代謝で記憶をなくした経験はない。<br />
「いやぁ〜、春の新陳代謝で8キロも痩せちゃってさ!」などとゆー珍事もない。<br />
細胞は自分とウリフタツの分身を作り、それを自分のアトガマにすえ、雇い主の人間に感謝されるコトも気づかれるコトもなく、確実な仕事をして、ひっそりと消える…なんとオクユカシー生き方なんだろうかッ!!<br />
しかも冒頭で書いたよーに、皮膚の細胞は死んでからも柔軟なヨロイとなって、紫外線や外傷から人間を守ってくれているのだ。<br />
だから、人間は細胞に感謝を込めて、お肌を大切にしなければならない。<br />
最近、ナノ化粧品などと呼ばれる商品が人気だが、それは化粧品の分子をナノメートル(nm)レベルまで小さくしたとされる化粧品だ。<br />
1nmは1mの「10億分の1」で、1万円札の厚みが0.1mm=10万nm、インフルエンザウィルスの大きさが80nmだから、まー、とにかく、とてつもなく1nmは小さい。<br />
多くの女性が大好きなコラーゲンの分子は300nmあり、こんなに「大きい分子」だと死んだ細胞の表面を濡らすくらいで、潤い効果は長つづきしない。<br />
ちなみに、お馴染みのH2O=水の分子は0.3nmだから、コラーゲンは水の1000倍も大きい分子になる。<br />
<br />
しか〜し!<br />
なんらかの方法でコラーゲン分子を小さく小さくすると、角質層の隙間に入り込んで保湿効果が長持ちする。(コラーゲンは基本的に30種類ほどあり、最近のバイオ技術でコラーゲンの特徴を持った「サイズの小さいコラーゲン」を完成させたと、各メーカーは言っているのだ)<br />
ワタル自身も風呂上がりなんかに、ビビアンが使っているナントカ・コラーゲン液を使ったりすると、確かに保湿効果が長い感じがする。<br />
<br />
しか〜し!<br />
だからと言って、コラーゲンが皮膚から体内に取り込まれたワケじゃない。<br />
長風呂をした時に、指先がシワシワになった経験はないだろうか?<br />
それでも数分後にシワシワが消えるのは、風呂の湯が体内に染み込んだんじゃなくて、異物として皮膚から強制退去させられたからだ。<br />
でなけりゃ全世界のサーファー達は、海水が全身に取り込まれて死んじゃうからね。<br />
