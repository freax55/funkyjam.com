<font color="#FFCC00">「14-15リーガ・エスパニョーラ」（WOWOWにて独占放送）の<br />イメージソングに新曲「Vamos」が決定！</font><br />
<img src="/images/common/dot.gif" width="1" height="8" /><br />
2014年8月23日よりWOWOWにて絶賛放映中の「スペインサッカー14-15シーズン」にて新曲「Vamos」がイメージソングに決定！<br>
軽快で元気を誘うこの曲は選手たちへの応援はもちろんのこと、聴いた人すべてへの人生応援歌となってます！タイトル「Vamos」とはスペイン語で「行け！」「進め！」といったサッカーではおなじみのエールのひとつです。&nbsp;■□■□■□■□■□久保田利伸新曲とリーガ・エスパニョーラに向けてのコメント■□■□■□■□■□「サッカーのスーパースターのプレイを観ると、ものすごくストレス解消になります。曲作りなどで煮詰まっている時など尚更です。メッシ、ネイマールが大好きですが、今年からそこにハメス・ロドリゲスが加わりました。彼らのミラクル・プレイは魔法に触れているようで最高に上がります。今回の曲はそんな思いへの感謝と敬意を込めて作らせて頂きました。」&nbsp;なおこの新曲の発売は未定です。しばらく放送でお楽しみ下さい。<br>
ゲーム及び新曲についての詳細はコチラ!!<br>
