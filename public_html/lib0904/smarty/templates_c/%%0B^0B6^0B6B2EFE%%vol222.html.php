数回前に予告していた、酸素カプセルの体験報告をしま&#12316;す。<br />
ワタル自身が通うコトになった酸素カプセル屋さんは、銀座・歌舞伎座近くのビルにあり、もーそれだけで「一流」や「高級」や「セレブ」といってサシツカエない。<br />
エレベーターを降りると、いきなり薄暗い部屋の中。種類は分からないが、グリーン系のアロマが適度に漂う。<br />
タイトな黒い制服姿のオネーサンが3人ほどいて、やはり「一流」や「高級」や「セレブ」といってサシツカエない。<br />
酸素カプセルは横置きのタイプだけだと思っていたが、そこには縦置きのカプセルもあり、中で加圧トレーニングも出来るとのコト。<br />
「でも最初は横になったほーがヨロシーと思います」とオネーサンに言われ、サカラウ理由もないので酸素カプセル横置きを選択する。<br />
カプセルのサイズは長さ2m強&直径1m弱で、デブ族のワタル自身でも寝たまま本を読めるヨユーがある。<br />
この中に、1.2気圧の酸素が充填され、寝ながらにして高気圧トレーニングしちゃうとゆーお手軽便利システムなのである。<br />
そーなると、酸素もアルプス山脈の無垢な酸素とか、白神山地のフィトンチッドたっぷりの酸素だろーと思っていたら、「この部屋の酸素を1.2気圧に加圧します」とゆーコトで、酸素は銀座の現地調達だった。<br />
ちなみに、最初は3日に1度のペースで1回50分のInto The酸素カプセルが効果的だと言われたが、ワタル自身は今までの3週間で4回しか行けていない。<br />
カプセルに入ると、徐々に気圧が高くなるのが分かる。<br />
なぜなら、気圧が高いので飛行機に乗った時みたいに、耳が詰まったよーな感じになるのだ。だから耳抜きが必要で、それが出来ない人は耳が痛くなって酸素カプセルどころじゃないから注意してちょーだい。<br />
3分ほどで1.2気圧になると、あとは寝てたり本を読んだりスマホいじったりの50分。<br />
ただそれだけで、酸素カプセルには素晴らしー効果があると、パンフレットには書いてある。<br />
良いコトだらけの一部を書いてみるね。<br />
&#9899;病気予防&改善　&#9899;体脂肪燃焼　&#9899;睡眠促進　&#9899;脳の活性化　&#9899;ストレス解消　&#9899;美肌効果　&#9899;アンチエイジング……<br />
どーよ、どーなのよ、この万能薬的効果は、どーなのよ!?<br />
