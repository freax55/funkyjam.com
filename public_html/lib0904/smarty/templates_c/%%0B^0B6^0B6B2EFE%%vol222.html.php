<br />
しか&#12316;し!<br />
パンフレットには書いてなかったけど、オネーサンが笑顔で言ったのだ。<br />
「酸素カプセルの後は食事が美味しいですよ。お酒も美味しくて何杯でも飲めちゃいます!』は、実際にソノトーリだった。<br />
<br />
しか&#12316;し!<br />
オレの場合、いつでも食事と酒はウマいから、やはり酸素カプセルの効果は不明のままなのである。<br />
でも、まだ8回分の無料権があるから、効果実感報告が出来るよーに、もーしばらくInto The酸素カプセルしてみる所存。<br />
