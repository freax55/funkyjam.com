<center><img src="../image/misia.jpg" /></center>
<img src="../../../../images/common/dot.gif" width="1" height="10" /><br />
久保田がラッパーKREVAとのコラボレーションを経て次に興味を抱いたアーティストが、圧倒的歌唱力を誇る世紀の歌姫<font color="#ffcc00"><strong>MISIA</strong></font>でした。<br />
以前に楽曲を提供したことなどもあり、ぜひ一緒に歌いたいという久保田からのラブコールにより実現したコラボレーションです。<br />
<br />
実は話が具体的に動き出す前から、曲のイメージはある程度できており、サビのメロディも久保田が暖めていたものがありました。そしてレコーディングでMISIAのパフォーマンスの高さに反応するように、ワンフレーズ、ワンフレーズがブラッシュアップされていきました。<br />
レコーディング中は常に高い集中力を発揮するMISIAに対し、久保田は「全ての曲を自分のものにする力がある。MISIAとの競演だからこそできることがある。」と大きな手応えを感じたようです。<br />
<br />
そしてこのスケール感のあるラブソングをアレンジしているのは、久保田と同じくファンキー・ジャムに所属するシンガーソングライターの森大輔です。クラシカルかつアカデミックな楽器の積み重なりが心地よいサウンドを作り出しています。<br />
<br />
本物のアーティスト同士が自由に響き合い、お互いが刺激し合いながら完成した王道ラブバラードです。
