しか〜し!<br />
ワタル自身はピンでもキリでも、やっぱり観光地が苦手である。<br />
オアフ島には20年ほど前に行ったコトがあるが、あまりに完璧な観光地で、も〜2度とハワイには近づかないと即決し、今もそのココロは変わらない。<br />
<br />
しか〜し!<br />
ハワイ島やマウイ島、カウアイ島などは、今もInto The Wildな世界が残っていると聞くので、ビビアンが買ったサマージャンボ宝くじが当たったら、行ってみたいと思う次第^_^。<br />
今回のフィジーでは、砂浜に掘った穴に焼けた石を詰め、バナナの葉などを敷いて、地産の野菜や肉や魚や果物なんかを蒸し焼きにする「LOVO」(ロボ)とゆー料理を、どーしても食べてみたかった。<br />
