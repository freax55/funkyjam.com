今は久保田利伸ファンクラブ会報の編集締め切り時期で、仕事やバイトで大変なコト、困ったコトは多々あるけれど、すべては想定内で解決&落着する。<br />
今のワタル自身は、想定外の事件や事故に巻き込まれなければ、東京のデコボコを日常として受け入れられ、少しの不便も不安も感じない。<br />
働き疲れたら、酒を飲みに行く場所もあるし、一緒に酔ってくれる友もいて、欲しい情報はiPadで手に入る。<br />
オレにとって東京は、ヤシの木のないワイキキなのだ。<br />
