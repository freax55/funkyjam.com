<font color="#FFCC00">久保田利伸の盟友：松尾潔氏が初の音楽専門書を発表！</font><br />
<img src="/images/common/dot.gif" width="1" height="8" /><br />
音楽評論家として久保田利伸の作品を代弁し、作詞やRe-mix等で作品そのものにも参加。そしてR&amp;Bへの造詣を余すところなく伝えるラジオ番組での共演などなど…長年に渡る友情を育んで来た松尾潔氏の新刊をご紹介します。<br>
<br>
･<a href="/image/image_test.php?path=/artist/kubota/news/image/matsuo_book.jpg&cr=100"> 画像はｺﾁﾗ </a><br>
松尾潔のメロウな日々著者：松尾潔発売：2014年5月30日体裁：四六判/並製/320頁価格：本体1800円＋税ISBN：978-4-907435-03-5発行：スペースシャワーネットワーク<br>
<br>
珠玉の銘文エッセイにして評論そしてヒストリー。胸いっぱいのR&amp;B愛が詰まった、音楽書籍の決定版。<br>
EXILE、平井堅、CHEMISTRYから久保田利伸、鈴木雅之、JUJUまでを手掛け、若くしてJ-POP〜J-R&amp;B界におけるリヴィング・レジェンドとなったKCこと松尾潔。これは、そんな松尾潔が満を持して放つ、初めての音楽専門書だ。R&amp;B愛に任せて突っ走った熱き日々、90年代。戻らないあの頃を振り返りながら本場アメリカ産R&amp;Bの真髄について説いた本書は、真摯な愛に満ちたブラック・ミュージック論であり、90年代アメリカ音楽界の貴重な現場証言集でもある。日本語で書かれたR&amp;B関連書籍としては間違いなく最高にして最強。<br>
◆本書内容◆<br>
第1部　メロウな日々Timeless Journey月刊誌〜ウェブサイト「bmr」の連載「松尾潔のメロウな日々」を大幅加筆した完全改稿決定版コラム<br>
第2部　忘れじの日々　Back in the Days90年代、日本でのR&amp;B人気を決定づけた名作・傑作アルバムたち。そのブックレットには、いつだって松尾潔の入魂解説が載っていた。そこから選りすぐった、ベスト・オブ・ベストなライナーノーツ集<br>
第3部　ベイビーフェイスを追いかけ　&nbsp;Searching for Babyface<br>
<br>
【プロフィール】 松尾潔　Kiyoshi"KC"Matsuo1968年1月4日生まれ　福岡県出身音楽プロデューサー/作詞家/作曲家　Never Too Much Productions代表早稲田大学在学中にR&amp;B/SOULを主な対象として執筆を開始。ジェームズ・ブラウン単独インタビューを始め、米英での豊富な現地取材をベースとした評論活動、多数のラジオ・TV出演を重ねる。久保田利伸との交流をきっかけに90年代半ばから音楽制作に携わり、SPEED、MISIA、宇多田ヒカルのデビューにブレーンとし参加。その後プロデュースした平井堅、CHEMISTRYをスターダムに関わり、K-POP市場拡大の原動力となる。その他、プロデューサー、ソングライターとしてEXILE、JUJU、由紀さおり、三代目J SOUL BROTHERSなど数多くのアーティストの楽曲制作に携わる。累計セールス枚数は3000万枚を超す。 
