 5月26日(月) 曇<br />
<br />
夜中にまた雨が降り、今日は朝から雲が低い。<br />
朝食は昨夜のツナ・ドミグラスの残りで済ませ、持参の麦茶&緑茶パックが大正解だと自画自賛。<br />
曇天だけど雨は降りそーにもないので、今回3度目のバイリキへ向かった。<br />
バイリキからさらにベシオ行きのバスを停め、ニッポン・コーズウェイを渡って、太平洋戦争の遺物=大日本帝国軍の砲台付近で下車した。<br />
<br />
しか〜し!<br />
まいった。<br />
まさか、泣くなんて、思ってもいなかった。<br />
ここ最近、真夏日の雪ダルマみたいに涙もろくなったのは認めるが、壊れた大砲を見て泣くとは…。<br />
<br />
しか〜し!<br />
それは、ただの壊れた大砲じゃなかった。<br />
外洋に向けられた砲身の先を臨むと、押し寄せる米軍の大艦隊が、クッキリと蘇るのだ。<br />
現存する大砲は2門だけだが、その先には砲座跡に建てられたらしき民家などもあり、戦時は多くの砲列が海を向き、まさに瀬戸際で米大艦隊を迎え撃っていたのだ。<br />
砲台の近くには分厚いコンクリートで固められた、おそらく弾薬庫も半壊で残っている。<br />
この、天国のよーなグラデーション・ブルーの島で、日本人もアメリカ人も、地獄の釜の蓋を開けたのだ。<br />
わずか70年ほど前、ここで5,000人近い日本兵が死んだ。<br />
その現場で今も、日本兵が決死の覚悟で撃ちまくった大砲が海を向いている。<br />
それを目の当たりにすると、大日本帝国の「狂気」が、「本気」だったと確信する。<br />
南の果ての孤島に、コンクリートで頑強な基地を造り、多量の兵器と兵士を送り、死ぬまでテッテーテキに戦わせるなんて、本気じゃなかったら出来やしない。<br />
そー思ったら、5,000人の命が虚しくて、哀しくて、あわれで、どーにもならず、涙になってしまった。<br />
慰霊碑に泣いた経験はないが、激戦の現場に残された、実物の凶器を目の当たりにして、オレは戦争の残酷と悲惨と無慈悲に、激しく打ちのめされたのだ。<br />
まいった…本当にまいった。<br />
ひとしきり泣いたあと、今日は陽射しが強くなかったので、全長約3kmのニッポンコーズウェイの徒歩制覇に再挑戦した。<br />
護岸壁の修復をしているが、道路もそこらじゅうで舗装がはがれてデコボコだ。<br />
他国の援助で立派な施設が完成しても、それを維持していくのは現地ピーポーだ。<br />
ウィリアムに連れて行ってもらった病院も研修センターも、アチコチが大きく壊れたままで、なぜ早く修復しなかったのか、日本人には不思議でならない。<br />
資金の問題もあるだろーが、現地ピーポーの意識の問題も小さくないと思うのだ。<br />
唯一、メンテナンスがゆき届いているのは、ラグーンを臨む国会議事堂。<br />
やはりとゆーか当然とゆーか、「国の顔」は最優先だ。<br />
キリバス伝統のヨットを模したデザインの国会議事堂は、国最大の建物だとゆーが、ワタル自身が通っていた大磯中学のほーがはるかに大きい^^;<br />
1時間ほどでコーズウェイを渡り終え、他に食事をする場所もないので、またまたパラソルレストランへ。<br />
ポーク系を頼みたかったんだけどNo Porkと言われ、「んじゃマッシュルーム・チキン。それってミックスベジタブル入ってる?」と訊くと、「当たり前でんがな」みたいな返事だったので、マッシュルーム・チキンwithoutミックスベジタブルをオーダー。<br />
出されたのは、中華風のシイタケとチキンのオイスターソース炒めonライス。問答無用にカンカンでありました。<br />
<br />
しか〜し!<br />
メニューの裏を見ると、Ramen SoupとかBeef Noodle Soupなんてのがある。<br />
多分120%、インスタント麺だと思うけど、ラーメン者としてはかなり気になる。<br />
追加注文しよーか激しく悩んで周囲の4L5L6L7Lを見渡すと、ダレも2品は頼んでいない。<br />
これはもしや、2品注文マナー違反inキリバスかもしれぬ。<br />
それに、1皿のライスの量が日本の大盛レベルなのだ。<br />
そーだそーだ、そーに違いない。<br />
2品頼まなくていーよーに、基本的にライス大盛なのだ。<br />
ふぅぅ〜、危うくマナー違反で逮捕され、炎天下砂浜引き回しの刑に処されるトコだったよ(・・;)<br />
<br />
しか〜し!
世界中ドコへ行ってもSoup Noodle関係は気になるので、もー1度来てトライしよーとココロに決めた。<br />
で、4時頃にロッジへ戻り、付近をブラブラしていたら、透明のガラス瓶に白い液体が入ったSomethingが、道端の売店小屋に並べられていた。<br />
もしやコレはと接近すると、白濁りの液体に、いかにも辛そーな青トーガラシが数本漬け込まれ、丸めたバナナの葉で栓がしてある。<br />
しかも、液体が発酵していて、プチプチと泡立っているではないかッ!<br />
すすす素晴らしー光景! コココレは、どー見てもキリバスの濁酒=ドブロクに違いない!<br />
値段を訊くと1.5ドル。キリバスの通貨はオーストラリアドルで、150円程度だ。<br />
すぐに買って、その場で瓶からラッパ飲みにしよーとしたら、店のオヤジが「No! No! No!」と猛スピードで叫んだ。<br />
「旅の人、そりゃ飲み物じゃありませんぜ。そりゃ、魚を食べる時に使う調味料ですぜ」<br />
とゆーワケでサバ缶を買い、ロッジの部屋でやってみた。<br />
あのね、ジョーダン抜きに、魚にピッタリのノンオイルドレッシング。<br />
出来るコトならサバ缶じゃなくて、ヒラメの薄造りとかの、白身新鮮系でやりたかった。<br />
これは「ビィニカ」とゆー調味料で、キリバスではポピュラーなんだって。<br />
作り方も訊いたけど、結局理解不可で、味から想像すると、ココナッツの樹液に酢を加え、砂糖と塩を少々、庭に生えてる青トーガラシを5〜6個入れて、最後にバナナの葉を丸めて、適量出るよーに栓をして完成…みたいな感じかな。<br />
発酵してるのは、ヤシ酒の原料にもなるココナッツのせーだと思う。<br />
そーそー、今日はじめてバスのFMラジオで日本人の歌を聴いた。<br />
ワタルの知らない女性シンガーで、♪Take Me Your Way♪ってのが決め歌詞なんだけど、他はちゃんとした日本語で、やっぱり異国で聴く日本の歌手って、ジャンル無関係に嬉しくなるよね。<br />
あのね、どのバスに乗ってもアンプ限界じゃねーの音量でFMを流してる。<br />
ほとんどの曲がドンジャガレゲェor熱唱バラード系で、これがまた、キリバスのグラデーション・ブルーにピッタリなんだな〜^o^<br />
聴き覚えのある曲が流れ、「えーと、これナンだっけ…?」って思い出したら、ジョン・デンバーの朗々たるフォークソング「悲しみのジェットプレイン」がゴリゴリのレゲェになってたりして、それはそれで気分Up↑になるのだ。<br />
バスにも慣れた。ミックスベジタブルの攻略法も分かった。<br />
<br />
しか〜し!<br />
どーにもならないのが、氷だよ、氷!<br />
ロッジにもパラソルレストランにも売店小屋にも、ドコにも氷は売ってない。<br />
港に揚がった漁れたての魚も、氷なしのクーラーボックスに入れて売ってるし、キリバスに来て、1度も氷を見たコトがない。<br />
唯一の手段は、部屋の冷蔵庫で作るんだけど、夜中と昼間に停電するコトが日常サハンジで、なかなか氷が出来ないんだよ(ー ー;)<br />
今キリバスにガリガリ君があったら、1本500円、いや、1000円…じゃ買わないけど…700円までなら買うよ。リアルに買うっつーの。食いてぇぇぇ（≧∇≦）<br />
これもウィリアム情報なんだけど、キリバスを走っている自動車は100%近く日本車なんだけど、そのほとんどが日本→タイ→キリバスの中古なんだって。<br />
日本→キリバスの直中古は高くて買えないんだって。<br />
同じ理由なのか、日本製の日用品も見かけない。<br />
唯一、自信を持って日本製と確信したのは、売店小屋で水を3本買って「袋ちょーだい」って言ったら10円取られた、そのビニール袋。「いい暮らし届けたい〜ナイス〜本部=秋田市新屋豊町××番地」ってプリントしてあった^o^<br />
ワタル自身の印象だと、オーストラリアとニュージーランドは当然として、アジア圏では中国とインドと韓国の日用製品がアットーテキだ。<br />
あのさぁ、関税とか輸入規制とか難しーコトは分からないけど、ガリガリ君とかキリバスでヒットするんじゃねーの? <br />
赤城乳業さんに、この原稿を送ってみよーかな?<br />
アッ! やっぱダメかも。だって昼や夜に停電になったら、さすがのガリガリ君も溶けちゃうよね!? <br />
そーかー、電力事情が改善しなくちゃどーにもならないのかぁ…。<br />
キリバスに原子力発電所を造るワケにもいかないし…ガリガリ君からキリバスの苦悩が見えたね…。<br />
