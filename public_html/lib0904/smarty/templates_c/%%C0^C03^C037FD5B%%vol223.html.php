実はねぇ、NHK朝の連続テレビ小説「マッサン」…見ちゃってるんだよね。<br />
最近は、中島みゆきさんが歌うテーマ曲「麦の唄」がはじまると次の展開を思いやり、早くも涙腺がウルウルしちゃうほどなの（≧∇≦）<br />
物語は、日本で最初にウィスキーを造った亀山政春氏と妻・エリーちゃんの泣き笑い奮闘記で、その亀山政春=マッサンを演じている主役の玉山鉄二さんが、以前からワタル自身のヒイキなのだ。<br />
役者によっては、役柄に違和感を感じる場合もあるけど、玉山さんはどんな役でも次第に「イカニモ」な雰囲気をかもしだす。<br />
失礼な言い方かもしれないが、完璧じゃない余白が、「イカニモ」を感じさせるのだ。<br />
ワタル自身は、今まで様々な分野で一流と呼ばれる人達にインタビューをして来たけれど、誰もが自分のスタイルに自信を持っているからこそ、誰もが完璧じゃなかった。<br />
ある数学者はコッペパンをかじりながら日本の教育を語り、ある政治家は腰が痛いと机に座って子供の未来を憂い、あるスポーツマンはアインシュタインの相対性原理を堂々と勘違いし…インタビュー中はナンダコイツハと思うのだが、原稿を書く時にはイカニモと納得してしまう^_^;<br />
オレは芸能評論家じゃないから玉山鉄二さんの将来は語れないが、今後の成長が楽しみな役者さんなのだ。<br />
さらに、連続テレビ小説初の外人ヒロインとなるシャーロット・ケイト・フォックス嬢(マッサンの妻・エリー役)も、金髪白人にはダラシのないワタル自身のツボだ。<br />
これまた失礼な言い方かもしれないが、65点くらいの美人度数がリアルなのだ。<br />
65点の美人度は、角度や表情によって80点にも90点にもランクアップしちゃう。<br />
さすがに100点満点はキビシーが、シャーロットちゃんは新婚のダンナが恋しくなって一時帰国したなどの実生活エピソードを聞くと、スコットランドから日本に嫁いだ実在のエリーちゃんと重なり、テレビの前で「負けるなよ!」と応援してしまう。<br />
マッサンの元婚約者・田中優子=相武紗季ちゃんはイジワルな設定だけど、ワタル的にはストライクの女優さんだし、彼女の父親・大作=西川きよしさん&母親・佳代=夏樹陽子さんもみんなWelcomeな人達なので、「マッサン」を見て泣いたり笑ったりすると、酸素カプセルより何倍もストレスフリーになれる。<br />
その中でも、「マッサン」のナニがジ〜ンと来るかって、エリーちゃんのヒタムキな姿&マッサンへの純な愛に尽きる。彼女のカタコトの日本語も、実にリアルで効果的なのだ。<br />
第12話なんて、優子に超イジワルされたのに、自分がスコットランドに残して来た母親への心痛を乗り越え、逆にマッサンを叱咤するエリーちゃんに、全国の朝が感涙に包まれた…かどーかは分からないけど…「あまちゃん」とは別種のベクトルで「マッサン」にハマっている。<br />
バツ2のワタル自身がゆーのもナンだけど、日本人どーしの結婚だって考え方や生活習慣の差異は避けられないのだから、国際結婚は当事者以外の周囲にもBIG LOVEが必要だ。<br />
まして、マッサンがエリーと共にスコットランドから帰国したのは1920年代。外国人だとゆーだけで、エリーとの結婚を否定した人も多い時代だったが、それ以上に多くの理解者がいたから、厳しい試練に遭いながらも、マッサン&エリーは日本最初のウィスキーを完成出来たのだ。<br />
もちろん、そーした幸運は、マッサン&エリーの愛と信念が呼び込んだのだ。<br />
