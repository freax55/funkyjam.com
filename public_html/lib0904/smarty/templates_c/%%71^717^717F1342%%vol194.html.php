40年振りくらいに、ジーンズのリペアでミシンを使った。<br />
ワタル自身が大学生の頃は、パッチワークのジーンズがHIPな時代で、市販品を買うより、自分メイドのオンリーワンがステイタスだった。<br />
だから近頃人気のダメージ加工ジーンズなどは、ゼッッッタイ買わない。<br />
ハナからボロいジーンズなんて、他人が噛んだガムを買うよーなモンで、噛んだら、いや、買ったら負けだッ! だッだッだッ!<br />
70年代のジーンズ者は、ワンウォッシュもしてないインディゴブルーを長年履きつづけ、マイ・スタイルに熟成させるのだ。<br />
今回のジーンズも「LA・LA・LA LOVE SONG」時代からの20年モノで、とーとーヒザが破れてしまったのだ。<br />
ワタル的には、最近の一般ヤングみたいに破れたままでもOKなんだけど、95歳の母親が「オマエはそんなにビンボーなのか？」と嘆くので、ビビアンのミシンを借りた次第。<br />
こんな作業は簡単だと思っていたら、21世紀ミシンのハイテク機能に戸惑い、両ヒザで6時間近くもかかり、ヘロヘロになってしまった。<br />
ミシンをかけながら痛感したんだけど、自分のファッションセンスは、20代からゼンゼン進歩してない（≧∇≦）<br />
安い雑巾みたいにジャミジャミに縫ったリペア部分で、「コレだよコレ! この感じだよ!」と、しみじみ悦に入ってしまうのだ。<br />
光モノ…と言っても、頭髪の貧しいオジサンや、アジやコハダなどの寿司ネタじゃない…シルバーアクセサリーなんかの光モノも、いまだに大好きだ。<br />
ワタル自身が10代の頃に、アメリカン・ロックと一緒にカジュアルな貴金属が来日し、20代のサーフィンブームで定着した。<br />
当時創刊された雑誌「ポパイ」はサーフィンのメッカとして、都心に近い湘南をローカルからセンターへ引き上げた。<br />
湘南ピーポーは突然のブームに浮き足立ちつつ、サーフィンと音楽とファッションの発信地に祭り上げられた地元が、実は激しく誇らしかった。<br />
その時代から、ジーンズとアロハシャツとシルバーは、不動のアイテムとして40年もワタル自身に君臨している。<br />
海辺の音楽は、ドゥービー・ブラザーズで決まりだった。<br />
マイケル・マクドナルドが参加する前の、初期のドゥービーだよ。<br />
群雄割拠の西海岸ロックの中でも、ドゥービーのパワフルなコーラスとカラフルなビートと乾いたギターが、ワタル自身のツボだったし、ドゥービー=Doobie=マリファナのスラングだから、みんなドゥービー・ブラザーズでHIGHになった。<br />
見つかればオマーリサンに捕まるよーなコトを繰り返しながら、70年代の湘南不良ピーポーは、少しずつフツーの一般ピーポーに脱皮していったのだ。<br />
<br />
しか〜し!<br />
ワタル自身は今も、湘南の緩い南風の中にいるよーな気がして仕方ない…なんてコトを、40年振りのミシンで深く思ったのである。<br />
3日後、リペアした雑巾ジーンズとアロハで母親を見舞うと、思いもしない開口一番。<br />
「今日はシャレた格好だね!」<br />
