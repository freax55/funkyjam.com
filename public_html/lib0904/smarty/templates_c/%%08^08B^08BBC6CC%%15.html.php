開園15周年を迎える「ハーベストの丘」から番組公開生放送♪ゲストによる生ライブも！<br>
<br>
更に、公開生放送に続いて、番組公開収録も決定！<br>
森 大輔と中村 中が映画音楽のカバーも交えてライブを披露！<br>
<br>
<br>
大阪府堺市の南に位置する「ハーベストの丘」は、甲子園8個分の広さを持ち、<br>
花・緑・動物など自然とふれあえる体験型の農業公園。<br>
ピザやパン作り体験・イチゴ狩り・BBQ・ソリすべりなど、1日いても飽きないアトラクション満載で<br>
ハーベストの丘自社商品の飲むヨーグルトやソーセージなどを購入することもできます。<br>
そんな「ハーベストの丘」の魅力を現地からたっぷりとお届けします。<br>
