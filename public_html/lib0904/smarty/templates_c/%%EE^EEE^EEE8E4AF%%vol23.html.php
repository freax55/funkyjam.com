第23回、今回のNo Diggityは、久保田さんが「この冬買ったモノ」をご紹介。<br />
<br />
<center><font color="#FFBF17">その1「Y-3（ワイスリー）のスニーカー」</font></center><br />
<b>〇どこで買ったんですか？</b><br>
「冬に履ける靴で今よく履いてるジーパン(スリムストレート)の流行がなかなか終わらないので、この形に合う靴を探してたんです。NYを歩くと流行の先読みが出来るので、街を歩いていたら、わりとかっこいいヤツは大体ハイカット気味の靴だなぁと思ったので、それをNYのダウンタウンで探してたらY-3に辿りついて、そこで買いました。Y-3が好きなんだけど、かっこいいヤツは皆黒履いてるなーと思ってこれに決めました。」<br>
<br>
<center><img src="/premium/artist/essay/kubota2/images/201603101.jpg" /> <img src="/premium/artist/essay/kubota2/images/201603102.jpg" /></center>
<br>
<b>〇黒履くの珍しいですよね？</b><br>
「そのとおり。スニーカーとしては黒は30年振りに買いました。<br>
この20年間は大体白が中心だったんだけど、あとはアディダスの遊びカラーとか。<br>
黒は30年前にReebokのスニーカーが流行ってた時は黒ばっかりだったんだけどね。<br>
とにかく30年ぶりに黒買いました。<br>
で、Y-3に行ったらビックリするほど全部黒で、いいなーと思って、もっと突飛なデザインもたくさんあったんだけど、そういうのだと時代に迎合しているみたいでチャラくて嫌だったから何かないかなぁと思ってね。でこれだ！っていうのがあったので買いました。」<br>
<br>
<b>〇履きやすいですか？</b><br>
「僕の足にはY3が合うので履きやすいです。<br>
すっごい軽いしやわらかいので足が疲れません。<br>
さすが、ファッションブランドとスポーツブランドのコラボブランドなだけあるよね。ちなみにY-3の「３」はアディダスの三本線だからね？YはYohji Yamamotoっていう日本人デザイナーの頭文字。」<br>
<br>
<br>
<b>【おまけ】<br>
〇白い靴をたくさん履いてますが、いつもきれいに履いてますよね？</b><br>
「あまり歩かないからじゃない？(笑)　通勤とかもしないし実際そうだと思う。あとは白い靴の場合、布製のものは基本選ばないし、革とかビニールが多いから汚れにくいんだと思う。あとは布製のヤツにしても最近は汚れ&防水スプレーを吹いて予防してる。<br>
とにかく一番は電車出勤しないからだと思う！<br>
あとは昔は気を付けなかったけど、最近は靴脱ぐ時とかに気を付けるね。<br>
足でひっかけずに手で脱ぐようにしてる。」<br>
<br>
