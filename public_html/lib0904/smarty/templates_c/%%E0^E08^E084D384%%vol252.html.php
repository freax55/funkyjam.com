「太陽寺院」も「ヘヴンズ・ゲート」も比較的新しい組織で、70年代にアメリカの西海岸で生まれたニューエイジ(New Age)思想に根ざしていると言われる。<br />
簡単にゆーと、物質文化より精神文化を優位とみなす「新しい時代」=「afterキリスト教」を受け入れるのが、ニューエイジ思想だ。<br />
