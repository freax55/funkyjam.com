BROWN EYED SOUL<br />（ブラウン・アイド・ソウル）<br />
</center>
<br />
2002年日韓共催FIFAワールドカップ公式ソング「Voices of KOREA/JAPAN」の韓国代表として抜擢されたBrown EyesのメンバーNaul（ナオル）が、よりディープな音楽表現を目指し結成したのがBROWN EYED SOULである。<br />
リーダーのジョンヨプ、ナオル、ヨンジュン、ソンフンの4人は、全員が作詞・作曲家であるという奇跡のR&amp;Bボーカル・グループと呼ばれ、2003年「Soul Free」で華々しくデビューを飾る。ほとんどプロモーション活動を行わず、アルバムレコーディングに1年以上費やすという寡作ぶりにも関わらず、その人気・実力は不動であり、リリースの度に必ず上位チャートにランキングされるという快挙を遂げている。<br />
4人それぞれが積極的にソロ活動も行い、個々の個性を生かした作品を発表する事によってファンの裾野を広げ、また数々の人気ドラマのOST（テーマ曲）に多用され、グループとして、ソロとして知名度を上げ続けている。<br />
テレビや取材に滅多に対応しない事からミステリアスなイメージを世間に与えつつも、余す事なくその歌唱力、音楽性、作曲能力を見せつけるコンサートでは、必ずチケットの争奪戦が発生している。<br />
たくさんの名曲を放つ事からジャンルを超えてアーティスト達に愛され、カバーされる事も多く、アイドルグループでさえ最も歌唱力のあるメンバーがまず好んでカバーしたがる曲は彼等の音源である。<br />
カリスマ溢れる韓国R&amp;B界の至宝BROWN EYED SOULは結成10周年を迎え、2013年10月、満を持して日本デビュー。<br />
2014年2月、韓国にて4thアルバム「THANK YOUR SOUL Side-A」をリリース。結成11年目の幕開けを華々しく飾り、更なる躍進を遂げている。<br />
