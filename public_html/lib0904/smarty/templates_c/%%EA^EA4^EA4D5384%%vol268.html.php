音楽の好き嫌いは誰にでもある。<br />
好きな歌手を擁護し、嫌いな歌手をDISるのも自由だ。<br />
<br />
しか〜し!<br />
批判的な文言を「匿名」でネット発表するのは、嫌いな相手を暗闇で殴るのと同じで、ワタル自身はアンフェアに思えて仕方ない。<br />
もっともっとイタタマレナイのは、犯罪の被害者&親族を冒涜するよーな書き込みだ。<br />
まるで被害者自身が犯罪を誘発したかのよーに、汚い言葉をぶつける匿名者にこそ、ココロの闇を感じてしまう。<br />
そして、そーゆー匿名ピーポーが、あまりに多い現実に、昭和のオジサンは背筋が寒くなるのだ。<br />
なぜ、こんな時代になったのか?<br />
それはハッキリしている。<br />
インターネットが普及して、市井の誰もが自由に言葉を発信出来る時代になったからだ。<br />
匿名ピーポーが自宅で発した言葉が、推敲も校正も自問自答もなく、あまりに安易に世界中の人々に届いてしまう。善意の模倣は歓迎だが、匿名の悪意には正義が感じられない。<br />
その結果、驚くほど急速に、信じられないほど過激に、視野の狭い若い世代が、匿名の悪意をコピペしている。<br />
インターネットは、鉄腕アトムで育った昭和ピーポーには考えもつかなかった、画期的なインタラクティブ・ツールだ。<br />
そーした夢のツールを子供の頃から利用出来る平成ピーポーが、ワタル自身はうらやましくて仕方ない。<br />
だから、洋々たるインターネットの将来を、悪意のコピペで台無しにして欲しくないのだ…と、ココロ優しい鉄腕アトム的に願っているのだよ。<br />
で、今週の週刊ワタル自身を…アップロード!<br />
