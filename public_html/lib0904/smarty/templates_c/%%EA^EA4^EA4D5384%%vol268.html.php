「すべての文化は模倣から始まった」と言われるよーに、模倣は人間が持っている素晴らしい才能だ。<br />
人間に限らず、多くの霊長類が親や仲間の行動を真似るのは、ミラーニューロンとゆー神経細胞の働きで、幼児が言葉を覚えるのも、下北半島のニホンザルが温泉に入るのも、個々の思考や判断ではなく、ミラーニューロン=モノマネ細胞に起因している。<br />
科学も芸術も先達の模倣からスタートし、新たな創造へ到達する。<br />
クラシック音楽界の巨匠=モーツァルトやシューベルト等も他者の曲をパクっていたと言われているが、それは原作者へのオマージュ&リスペクトに近い…と思いたい。(※1)<br />
そして現代のヒップホップミュージックに至って、堂々と過去の名曲をサンプリングし、それを時代のメインストリームにまで成長させた。<br />
これぞ、デジタル時代のコピー&ペースト=コピペ文化の典型だ。<br />
で、東京オリンピック2020のエンブレム問題で火がついた、佐野研二郎氏のデザイン盗用問題だ。<br />
今回のオリンピックは国立競技場の予算やボランティアのユニフォーム等々、残念な問題続出だったので、佐野氏デザインのエンブレムが発表された時、よーやくマトモなプロの仕事を感じられた。<br />
