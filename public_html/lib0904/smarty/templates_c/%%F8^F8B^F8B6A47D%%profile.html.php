<center>
久保田利伸（くぼたとしのぶ）<br />
<br />
静岡県出身　7月24日生まれ<br />
</center>
<br />
1985年、田原俊彦、小泉今日子、高橋真梨子等、アイドルから実力派歌手まで作曲家として幅広く楽曲を提供する。デビュー前に遊びで制作した「すごいぞテープ（洋楽曲のカバーなどを収録）」がマスコミの間で評判になり、非売品であった為そのコピーが巷に出回り、高額で取引される事件が起こる。<br />
<br />
1986年、レコード会社争奪戦を経て、Sony Music（旧CBS SONY）よりシングル「失意のダウンタウン」でメジャー・デビュー。<br />
オリジナリティ溢れる音楽性、質の高い楽曲、抜群の歌唱力、卓越したリズム感で多くの人々を引きつけ、ロック・ミュージック主流の当時の音楽シーンに新風を送り込む。<br />
<br />
1988年、アルバム「Such A Funky Thang!」がミリオンセラーに。海外レコーディングを経てなお、精力的に米国のミュージシャンと接点を持ち、自らの音楽性を高めてゆく。この頃から「地球を相手に歌ってゆきたい」という志を固めてゆく。<br />
<br />
1993年、NYに拠点を移す。<br />
1995年、USコロンビアレーベルより1stアルバムを発表。日本でのキャリアを意識せず、新人として米国で活動を始める。<br />
<br />
2004年、米国にて3作目となるアルバムを発表。この年、Soul Musicianにとって殿堂入りとされる米国老舗番組「SOUL TRAIN」に初の日本人ヴォーカリストとして出演し、賞賛を得る。<br />
<br />
2007年、日本において、カリスマを讃える「MTV Icon」に初のアーティストとして選ばれる。久保田をレスペクトする新進気鋭のミュージシャンやラッパー、ミリオンセラー・アーティストまで多数参加のイベントが行われる。<br />
<br />
2011年、この6月よりデビュー25周年を迎え、多種多様な試みに挑戦。2年連続コンサート・ツアー、オリジナルアルバムの発表等世間を賑わせ、集大成となる25周年アニバーサリー・コンサートツアー（15万人動員）、記念ベストアルバムは共に大成功をおさめる。<br />
<br />
2013年、新たな試みへの挑戦として、“もうひとりの久保田利伸”を表現する“Parallel World Series”『KUBOSSA』を7月3日にリリース。<br />
初の野外ソロとなるスペシャル・ツアーを成功に導く。<br />
<br />
現在2014年に向けて新作を制作中。<br />
<br />
当時から現在に至るまで、その音楽性にブレはなく、「Japanese R&amp;B」のパイオニアと呼ばれる。<br />
デビュー当時から自身が演出・プロデュースを手掛けるライブの完成度にも定評があり、その楽しさと感動は他に類を見ない。<br />
自身の音楽制作のみならず、ドラマ・映画主題歌・CM音楽・スポーツ祭典におけるテーマ曲・アニメソングなど、多くのメディアへの楽曲提供経験も豊富であり、それら目的、期待に十分応え得る比類無き仕事への姿勢とバランス感覚も持ち合わせている。<br />
2012年に至るまで、邦盤アルバム14作、US洋盤アルバム3作、ベスト盤5作（「the BADDEST」シリーズ）他を発表。どの作品も普遍性を持ちながら、時代を的確に掴み、確実にファンの期待に応えている。<br />
<br />
代表作<br />
「流星のサドル」「Missing」「LA・LA・LA  LOVESONG」「AHHHHH!」<br />
「LOVE RAIN〜恋の雨〜」「Bring me up!」等多数。<br />
