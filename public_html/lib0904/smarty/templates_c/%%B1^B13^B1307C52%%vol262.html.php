7月4日、久保田利伸の取材で青森へ行った。<br />
リンクステーションホール青森で行われたライヴの模様は、8月25日発行予定のファンクラブ会報「BARI BARI CREW」115号に譲り、ここでは青森初体験を報告する。<br />
まず、ワタル自身にとって青森県の特産物とゆーと、最初に大間のマグロ=黒マグロが浮かび、次にリンゴが来て、田子(たっこ)のニンニクとなる。<br />
<br />
しか〜し!<br />
東北のコンサートを仕切っているイベンター=キョードー東北の方が案内してくれたのは、マグロでもリンゴでもニンニクでもなく、豚肉が自慢の店だった。<br />
「青森で豚?」のワタル自身だったが、最初のヒトクチで、自分の無知を恥じ入った。<br />
青森の豚肉は、驚くほどウマかった。<br />
一番有名な銘柄は、ニンニクを飼料に配合した「奥入瀬(おいらせ)ガーリックポーク」と言われるが、その他にも多くのブランド豚があり、青森養豚業者の努力&研究が賞賛される。<br />
青森のブランド豚は総じてジューシーで味が濃く、なのに雑味が皆無で、蒸した豚肉をゴマ油+岩塩でやると、肉の旨味が深い余韻を残してくれる。たとえるなら、久保田利伸の名曲が、ゆっくりフェイドアウトしてゆくイメージ…としておこう。<br />
期待度数ゼロの地元ラーメン店で頼んだ「青森ラーメン」の、薄く切ったチャーシューまでも絶品で、青森豚の底力に脱帽となった。<br />
もちろん、ラーメンそのものも良かった。<br />
コッテリ派にはモノタリナイだろーが、澄んだ煮干しスープと正油の絶妙が、青森の「昔ながら」を思わせ、旅先のラーメンに最適だった。<br />
取材の翌日は、１人で路線バスの旅とした。<br />
青森駅から八甲田山へ行き、さらに十和田湖まで、車窓を楽しむコンタンだ。<br />
これが良かった。素晴らしかった。<br />
ワタル世代にとっての八甲田は、新田次郎の小説「八甲田山　死の彷徨」を映画化した「八甲田山」(1977年/森谷司郎監督)が記憶に鮮明だ。<br />
高倉健+北大路欣也+三国連太郎+丹波哲郎+緒形拳+加山雄三+etc…のオールスターキャストで撮影された「八甲田山」は、日本陸軍兵199人が遭難死した雪中行軍遭難事件(1902年)をベースにした作品で、猛吹雪に死を覚悟した神田大尉(北大路欣也)のセリフ…「天は我々を見放した」が流行語になるほど大ヒットした。<br />
当時大学生だったワタル自身も、ドイツ語のテストが100点満点で3点(≧∇≦)だった時に、「天は我々を見放した」と崩れ落ち、「我々じゃなくて我だけだろ!」とドイツ語担当につっこまれたりした。<br />
ソンなコンなで、199人もの命を奪った八甲田は恐ろしい山とゆー印象ばかりだったが、快晴の7月にロープウェイで登った八甲田は、見渡す限り深緑の流麗な連なりで、矮小化した異形の木々が、冬の苛酷を伝えるばかりだった。<br />
展望台から降り、山頂に立てられた地図を見ると、山あいに「グダリ沼」とゆー表記を見つけ、「わっ!」となった。井伏鱒二の短編集「川釣り」(1952年)にあった「グダリ沼」※を思い出したからだ。<br />
『さすが井伏翁、戦後間もない時期に、こんな所にまで釣行されていたのか…』<br />
日本兵の遭難死と井伏翁の釣狂に時の流れを感じつつ、八甲田から十和田湖へバスを乗り継ぐ。<br />
路線バスなので途中の温泉場や景勝地で何度も停車しながら、冬は7メートルもの積雪があるとゆー峠を越え、高山植物の自生地からブナの林を抜け、奥入瀬渓流と並走する絶景に至る。<br />
バス道のすぐ横を流れる奥入瀬渓流の、多彩な自然美は本当に見事で、車窓だけでも堪能出来たが、痛風の心配さえなければ下車して遊歩道を歩きたいほどだ。<br />
なので、国内旅行経験未熟者のワタル自身は、この奥入瀬渓流を日本美渓流(暫定)第一位とする。<br />
そんな美観に癒されて辿り着いた十和田湖は、初夏の山並に囲まれた静寂な湖で、予想していた繁華な観光地とは違っていた。<br />
観光客もまばらで、多くの宿泊施設がシーズン前で休業なのかと思ったら、2度と営業しません…的に荒れ果てていた。<br />
営業している店も活気が感じられず、十和田湖名産のヒメマスの塩焼と缶チューハイを湖畔でやり、本日の昼食とする。 <br />
<br />
しか〜し!<br />
それから再び、オノレの無知に恥じ入るコトになった。<br />
十和田湖畔の有名な「乙女の像」の作者が、高村光太郎だったなんて全然マッタク知らなかったからだ。<br />
高村光太郎は「智恵子抄」に代表される文学者のイメージしかなく、彫刻家&画家のプロフィールは知らなかった(ーー;)<br />
すぐにiPhoneで調べてみたら、父親は上野の西郷隆盛像を造った高村光雲(!!)…ほとほと自分の無学が情けなくなり、帰路も往路と同じルート3時間を、すきっ腹に缶チューハイで寝てしまった。<br />
翌朝、青森駅近くの「ねぶたの家　ワ・ラッセ」へ寄り、青森ピーポーの情熱に感電した。<br />
ねぶたには以前から興味があったが、本物を見たコトがなく、間近にしたねぶたは予想以上の求心力だった。<br />
さらにその歴史を知ると、冬の幽閉と夏の解放が強烈に撚り合った、青森ならではの、風土芸術=ねぶた祭りだと思わされた。<br />
そう思ったら、炸裂する夏の青森を観たくてタマラナクなった。<br />
ねぶた祭りの青森を実感したくて、ヒリヒリした。<br />
人の命さえ奪う豪雪と、清冽な渓流を育む原生林と、短い夏に燃え上がる人々がいる青森…国内旅行経験未熟者は、青森が大好きになってしまったのである。<br />
<br />
しか〜し!<br />
東京で仕事が待っている。<br />
いつか再訪のココロを残し、新青森駅から新幹線で帰路に就く。<br />
<br />
しか〜し!<br />
その前に、久保田バンドでキーボードを弾いている柿ヤン=柿崎洋一郎に教えてもらった「東北名酒の100円自動販売機」で、「じょっぱり」に舌ツヅミ。<br />
うん、青森は酒もウマイぞォ〜(*^◯^*)<br />
